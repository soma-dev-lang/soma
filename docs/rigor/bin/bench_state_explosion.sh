#!/usr/bin/env bash
# State-explosion bench for `soma verify`.
#
# Generates a series of synthetic state machines of increasing size,
# runs `soma verify` against each, times it, and writes a markdown
# table to docs/rigor/results/state_explosion.md.
#
# This replaces the hand-waved table in docs/SEMANTICS.md ┬¦3 with
# numbers that can be reproduced.
#
#   bash docs/rigor/bin/bench_state_explosion.sh
#
# Topology of the synthetic machines:
#
#   Linear (chain): s0 -> s1 -> s2 -> ... -> s(N-1) -> done
#     Worst case for path-finding (longest single path).
#
#   Diamond:        s0 -> {s1..s_k} -> s_{k+1} -> {s_{k+2}..} -> ... -> done
#     Stresses fan-out ├Ś fan-in. k = sqrt(N).
#
#   Cyclic:         like Linear but with a back-edge from sN/2 -> s1
#     Forces the verifier to detect cycles instead of cleanly terminating.

set -euo pipefail
cd "$(dirname "$0")/../../.."

SOMA="./compiler/target/release/soma"
WORKDIR="$(mktemp -d)"
trap 'rm -rf "$WORKDIR"' EXIT

OUT="docs/rigor/results/state_explosion.md"
mkdir -p "$(dirname "$OUT")"

# ŌöĆŌöĆ Generators ŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆ

gen_linear() {
    local n="$1"
    local dir="$WORKDIR/linear_$n"
    mkdir -p "$dir"
    {
        echo "cell C {"
        echo "    state lc {"
        echo "        initial: s0"
        local i=0
        while [[ $i -lt $((n - 1)) ]]; do
            echo "        s$i -> s$((i + 1))"
            i=$((i + 1))
        done
        echo "    }"
        echo "    on run() { return 0 }"
        echo "}"
    } > "$dir/app.cell"
    {
        echo "[verify]"
        echo "deadlock_free = true"
        echo "eventually = [\"s$((n - 1))\"]"
    } > "$dir/soma.toml"
    echo "$dir/app.cell"
}

gen_diamond() {
    local n="$1"
    local dir="$WORKDIR/diamond_$n"
    mkdir -p "$dir"
    # k = ceil(sqrt(n)), grouped fan-out
    local k=1
    while [[ $((k * k)) -lt $n ]]; do k=$((k + 1)); done
    {
        echo "cell C {"
        echo "    state lc {"
        echo "        initial: s0"
        local i=0
        # Layer 0: s0 -> s_1..s_k
        local j=1
        while [[ $j -le $k ]]; do
            echo "        s0 -> s$j"
            j=$((j + 1))
        done
        # Layer 1: each s_1..s_k -> s_{k+1}..s_{2k}
        local src=1
        while [[ $src -le $k ]]; do
            local dst=$((k + 1))
            while [[ $dst -le $((2 * k)) && $dst -lt $n ]]; do
                echo "        s$src -> s$dst"
                dst=$((dst + 1))
            done
            src=$((src + 1))
        done
        # Funnel layer 1 -> done
        local s=$((k + 1))
        while [[ $s -le $((2 * k)) && $s -lt $n ]]; do
            echo "        s$s -> done"
            s=$((s + 1))
        done
        echo "    }"
        echo "    on run() { return 0 }"
        echo "}"
    } > "$dir/app.cell"
    {
        echo "[verify]"
        echo "deadlock_free = true"
        echo "eventually = [\"done\"]"
    } > "$dir/soma.toml"
    echo "$dir/app.cell"
}

gen_cyclic() {
    local n="$1"
    local dir="$WORKDIR/cyclic_$n"
    mkdir -p "$dir"
    {
        echo "cell C {"
        echo "    state lc {"
        echo "        initial: s0"
        local i=0
        while [[ $i -lt $((n - 1)) ]]; do
            echo "        s$i -> s$((i + 1))"
            i=$((i + 1))
        done
        # Back edge: half-way -> early
        local mid=$((n / 2))
        echo "        s$mid -> s1"
        echo "    }"
        echo "    on run() { return 0 }"
        echo "}"
    } > "$dir/app.cell"
    {
        echo "[verify]"
        echo "deadlock_free = true"
        # NB: with the back edge, eventually(s_{n-1}) is FALSIFIABLE because
        # the cycle s1..s_mid loops forever. We expect this to FAIL ŌĆö measure
        # the cost of the falsification trace, not a successful proof.
        echo "eventually = [\"s$((n - 1))\"]"
    } > "$dir/soma.toml"
    echo "$dir/app.cell"
}

time_ms() {
    local file="$1"
    local start=$(python3 -c 'import time; print(int(time.time()*1000))')
    "$SOMA" verify "$file" > /dev/null 2>&1 || true
    local end=$(python3 -c 'import time; print(int(time.time()*1000))')
    echo $((end - start))
}

# ŌöĆŌöĆ Run benches ŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆŌöĆ

echo "# State-explosion bench results"             >  "$OUT"
echo                                                >> "$OUT"
echo "Generated by \`docs/rigor/bin/bench_state_explosion.sh\` on $(date +%Y-%m-%d)." >> "$OUT"
echo                                                >> "$OUT"
echo "Each row is the wall time of \`soma verify\` on a synthetic"     >> "$OUT"
echo "state machine of the given topology and size, including loading,"  >> "$OUT"
echo "parsing, and reporting. Numbers are single-run, so noise is real;" >> "$OUT"
echo "they characterise the cliff, not microbenchmark precision."        >> "$OUT"
echo                                                >> "$OUT"

run_topology() {
    local label="$1"
    local generator="$2"
    shift 2
    local sizes=("$@")

    echo "## $label"                                        >> "$OUT"
    echo                                                    >> "$OUT"
    echo "| states | wall (ms) |"                           >> "$OUT"
    echo "|-------:|----------:|"                           >> "$OUT"

    echo
    echo "==== $label ===="
    for n in "${sizes[@]}"; do
        local file
        file=$($generator "$n")
        local t
        t=$(time_ms "$file")
        printf "  %5d states ŌåÆ %5d ms\n" "$n" "$t"
        printf "| %5d | %5d |\n" "$n" "$t" >> "$OUT"
    done
    echo                                                    >> "$OUT"
}

run_topology "Linear chain"  gen_linear  10 50 100 500 1000 2000 5000 10000
run_topology "Diamond"       gen_diamond 10 50 100 500 1000 2000 5000
run_topology "Cyclic (eventually FAILS, measures counter-example cost)" gen_cyclic 10 50 100 500 1000 2000

echo "Results written to $OUT"
