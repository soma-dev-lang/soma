//! Generates Rust source from [native] handler AST.
//!
//! The output is a single .rs file with `#[no_mangle] pub extern "C"` functions
//! that can be compiled into a cdylib and loaded via FFI.

use crate::ast::*;
use std::collections::{HashMap, HashSet};

/// Type inferred for a local variable
#[derive(Debug, Clone, Copy, PartialEq)]
pub enum NativeType {
    Int,
    Float,
    Bool,
    String,
}

impl NativeType {
    fn rust_str(&self) -> &'static str {
        match self {
            NativeType::Int => "i64",
            NativeType::Float => "f64",
            NativeType::Bool => "bool",
            NativeType::String => "String",
        }
    }
    /// Type for computation inside [native] handlers — BigInt for ints
    fn compute_str(&self) -> &'static str {
        match self {
            NativeType::Int => "BigInt",
            NativeType::Float => "f64",
            NativeType::Bool => "bool",
            NativeType::String => "String",
        }
    }
}

/// Information about a native handler to be compiled
pub struct NativeHandler {
    pub cell_name: String,
    pub signal_name: String,
    pub params: Vec<Param>,
    pub body: Vec<Spanned<Statement>>,
}

/// FFI signature info: param types + return type
#[derive(Debug, Clone)]
pub struct NativeSig {
    pub fn_name: String,
    pub param_types: Vec<NativeType>,
    pub return_type: NativeType,
    /// If true, args are passed via shared buffer (_soma_push_*), not C params
    pub uses_shared_args: bool,
}

/// Parallel configuration for code generation
#[derive(Debug, Clone, Default)]
pub struct ParallelConfig {
    pub enabled: bool,
    pub handlers: Vec<String>,
    pub threads: usize,
}

/// Generate Rust source for a set of native handlers in one cell.
pub fn generate_native_source(handlers: &[NativeHandler]) -> (String, Vec<NativeSig>) {
    generate_native_source_with_config(handlers, &ParallelConfig::default())
}

pub fn generate_native_source_with_config(handlers: &[NativeHandler], parallel: &ParallelConfig) -> (String, Vec<NativeSig>) {
    let mut out = String::new();
    let mut sigs = Vec::new();

    // Build set of sibling native handler names for inter-call resolution
    let siblings: HashSet<String> = handlers.iter().map(|h| h.signal_name.clone()).collect();

    // Pre-scan: determine if any handler qualifies for Rug mode.
    // If ANY handler in this cell uses Rug, ALL Int-returning handlers use Rug
    // (to avoid conflicting shared buffer types).
    let mut any_qualifies_rug = false;
    let mut rug_handlers: HashSet<String> = HashSet::new();
    for h in handlers {
        let mut gen = FnGenerator::new(&h.params, &siblings, &format!("handler_{}", h.signal_name));
        for p in &h.params {
            gen.var_types.insert(p.name.clone(), type_expr_to_native(&p.ty.node));
        }
        gen.infer_body_types(&h.body);
        if should_use_rug(h, &gen) {
            any_qualifies_rug = true;
        }
    }

    // If any handler qualifies, promote all Int-using handlers to Rug
    if any_qualifies_rug {
        for h in handlers {
            let mut gen = FnGenerator::new(&h.params, &siblings, &format!("handler_{}", h.signal_name));
            for p in &h.params {
                gen.var_types.insert(p.name.clone(), type_expr_to_native(&p.ty.node));
            }
            gen.infer_body_types(&h.body);
            let uses_int = h.params.iter().any(|p| type_expr_to_native(&p.ty.node) == NativeType::Int)
                || gen.var_types.values().any(|t| *t == NativeType::Int);
            if uses_int {
                rug_handlers.insert(h.signal_name.clone());
            }
        }
    }

    let any_uses_rug = !rug_handlers.is_empty();
    // V mode only if there are Int handlers NOT covered by rug
    let any_uses_v = !any_uses_rug && handlers.iter().any(|h| {
        let mut gen = FnGenerator::new(&h.params, &siblings, &format!("handler_{}", h.signal_name));
        for p in &h.params {
            gen.var_types.insert(p.name.clone(), type_expr_to_native(&p.ty.node));
        }
        gen.infer_body_types(&h.body);
        h.params.iter().any(|p| type_expr_to_native(&p.ty.node) == NativeType::Int)
            || gen.var_types.values().any(|t| *t == NativeType::Int)
    });

    out.push_str("// Generated by Soma compiler — [native] handlers\n");
    out.push_str("// Do not edit — regenerate from .cell source\n");

    if any_uses_rug {
        out.push_str("use rug::Integer;\n");
        out.push_str("use rug::Assign;\n\n");
    }
    if any_uses_v {
        out.push_str("use num_bigint::BigInt;\n");
        out.push_str("use num_traits::ToPrimitive;\n\n");
    }

    // Check if any handler uses random()
    let uses_random = handlers.iter().any(|h| body_uses_random(&h.body));
    if uses_random {
        out.push_str("static mut _SOMA_RNG_STATE: u64 = 0x12345678_9abcdef0;\n\n");
        out.push_str("#[inline(always)]\nunsafe fn _soma_random() -> f64 {\n");
        out.push_str("    _SOMA_RNG_STATE ^= _SOMA_RNG_STATE >> 12;\n");
        out.push_str("    _SOMA_RNG_STATE ^= _SOMA_RNG_STATE << 25;\n");
        out.push_str("    _SOMA_RNG_STATE ^= _SOMA_RNG_STATE >> 27;\n");
        out.push_str("    let r = _SOMA_RNG_STATE.wrapping_mul(0x2545F4914F6CDD1D);\n");
        out.push_str("    (r >> 11) as f64 / ((1u64 << 53) as f64)\n");
        out.push_str("}\n\n");
    }

    let mut v_enum_emitted = false;
    let mut rug_shared_emitted = false;

    for handler in handlers {
        let mut gen = FnGenerator::new(&handler.params, &siblings, &format!("handler_{}", handler.signal_name));

        // Infer types from params
        for p in &handler.params {
            let ty = type_expr_to_native(&p.ty.node);
            gen.var_types.insert(p.name.clone(), ty);
        }

        // Pre-scan body to infer local variable types
        gen.infer_body_types(&handler.body);

        let fn_name = format!("handler_{}", handler.signal_name);

        // Build param list
        let param_str: String = handler.params.iter()
            .map(|p| {
                let ty = type_expr_to_native(&p.ty.node);
                format!("{}: {}", p.name, ty.rust_str())
            })
            .collect::<Vec<_>>()
            .join(", ");

        // Determine return type from body (look for return statements)
        let ret_type = gen.infer_return_type(&handler.body);

        // Does this handler use Int variables (needs V type)?
        let uses_int = handler.params.iter().any(|p| type_expr_to_native(&p.ty.node) == NativeType::Int)
            || gen.var_types.values().any(|t| *t == NativeType::Int);

        // Check if this handler should use Rug mode
        let use_rug = rug_handlers.contains(&handler.signal_name);

        if use_rug {
            // ═══ Rug mode: direct rug::Integer codegen ═══

            // Emit shared buffer infrastructure once (for rug mode)
            if !rug_shared_emitted {
                rug_shared_emitted = true;
                out.push_str("
// Shared arg/result buffer for rug mode
static mut _SOMA_ARGS: Vec<Integer> = Vec::new();
static mut _SOMA_RESULT: Option<String> = None;

#[no_mangle]
pub extern \"C\" fn _soma_clear_args() {
    unsafe { _SOMA_ARGS.clear(); }
}

#[no_mangle]
pub extern \"C\" fn _soma_push_i64(v: i64) {
    unsafe { _SOMA_ARGS.push(Integer::from(v)); }
}

#[no_mangle]
pub extern \"C\" fn _soma_push_bigint(ptr: *const u8, len: i64) {
    unsafe {
        let bytes = std::slice::from_raw_parts(ptr, len as usize);
        let s = std::str::from_utf8_unchecked(bytes);
        let val = Integer::parse(s).unwrap();
        _SOMA_ARGS.push(Integer::from(val));
    }
}

#[no_mangle]
pub extern \"C\" fn _soma_result_ptr() -> *const u8 {
    unsafe { _SOMA_RESULT.as_ref().map(|s| s.as_ptr()).unwrap_or(std::ptr::null()) }
}

#[no_mangle]
pub extern \"C\" fn _soma_result_len() -> i64 {
    unsafe { _SOMA_RESULT.as_ref().map(|s| s.len() as i64).unwrap_or(0) }
}
");
            }

            // Generate zero-arg handler that reads from shared buffer
            out.push_str(&format!("#[no_mangle]\npub extern \"C\" fn {}() -> i64 {{\n", fn_name));

            // Read params from shared buffer
            for (i, p) in handler.params.iter().enumerate() {
                let ty = type_expr_to_native(&p.ty.node);
                if ty == NativeType::Int {
                    out.push_str(&format!("    let mut {} = unsafe {{ _SOMA_ARGS[{}].clone() }};\n", p.name, i));
                } else {
                    out.push_str(&format!("    let {} = unsafe {{ _SOMA_ARGS[{}].to_i64().unwrap_or(0) as {} }};\n",
                        p.name, i, ty.rust_str()));
                }
            }

            // Generate body with rug::Integer
            for stmt in &handler.body {
                out.push_str(&gen.gen_stmt_rug(&stmt.node, 1));
            }

            let last_is_return = handler.body.last()
                .map(|s| matches!(s.node, Statement::Return { .. }))
                .unwrap_or(false);
            if !last_is_return { out.push_str("    0i64\n"); }
            out.push_str("}\n\n");

        } else if ret_type == NativeType::Int || (uses_int && ret_type == NativeType::String) {
            // ═══ V mode: i64→BigInt auto-promotion ═══

            // Helper: checked i64 ops that promote to BigInt on overflow — emit once
            if !v_enum_emitted {
                v_enum_emitted = true;
            out.push_str("
/// A value that starts as i64 and promotes to BigInt on overflow
enum V { S(i64), B(BigInt) }
impl V {
    fn add(&mut self, other: i64) {
        *self = match self {
            V::S(a) => match a.checked_add(other) {
                Some(r) => V::S(r),
                None => V::B(BigInt::from(*a) + other),
            },
            V::B(a) => { *a += other; return; }
        };
    }
    fn sub(&mut self, other: i64) {
        *self = match self {
            V::S(a) => match a.checked_sub(other) {
                Some(r) => V::S(r),
                None => V::B(BigInt::from(*a) - other),
            },
            V::B(a) => { *a -= other; return; }
        };
    }
    fn mul_v(&mut self, other: &V) {
        *self = match (&*self, other) {
            (V::S(a), V::S(b)) => match a.checked_mul(*b) {
                Some(r) => V::S(r),
                None => V::B(BigInt::from(*a) * *b),
            },
            (V::S(a), V::B(b)) => V::B(BigInt::from(*a) * b),
            (V::B(a), V::S(b)) => V::B(a.clone() * *b),
            (V::B(a), V::B(b)) => V::B(a.clone() * b),
        };
    }
    fn add_v(&mut self, other: &V) {
        *self = match (&*self, other) {
            (V::S(a), V::S(b)) => match a.checked_add(*b) {
                Some(r) => V::S(r),
                None => V::B(BigInt::from(*a) + *b),
            },
            (V::S(a), V::B(b)) => V::B(BigInt::from(*a) + b),
            (V::B(a), V::S(b)) => V::B(a.clone() + *b),
            (V::B(a), V::B(b)) => V::B(a.clone() + b),
        };
    }
    fn sub_v(&mut self, other: &V) {
        *self = match (&*self, other) {
            (V::S(a), V::S(b)) => match a.checked_sub(*b) {
                Some(r) => V::S(r),
                None => V::B(BigInt::from(*a) - *b),
            },
            (V::S(a), V::B(b)) => V::B(BigInt::from(*a) - b),
            (V::B(a), V::S(b)) => V::B(a.clone() - *b),
            (V::B(a), V::B(b)) => V::B(a.clone() - b),
        };
    }
    fn div_v(&mut self, other: &V) {
        *self = match (&*self, other) {
            (V::S(a), V::S(b)) => V::S(*a / *b),
            (V::S(a), V::B(b)) => V::B(BigInt::from(*a) / b),
            (V::B(a), V::S(b)) => V::B(a.clone() / *b),
            (V::B(a), V::B(b)) => V::B(a.clone() / b),
        };
    }
    fn mod_v(&mut self, other: &V) {
        *self = match (&*self, other) {
            (V::S(a), V::S(b)) => V::S(*a % *b),
            (V::S(a), V::B(b)) => V::B(BigInt::from(*a) % b),
            (V::B(a), V::S(b)) => V::B(a.clone() % *b),
            (V::B(a), V::B(b)) => V::B(a.clone() % b),
        };
    }
    fn to_i64(&self) -> Option<i64> {
        match self { V::S(n) => Some(*n), V::B(n) => n.to_i64() }
    }
    fn to_bigint(&self) -> BigInt {
        match self { V::S(n) => BigInt::from(*n), V::B(n) => n.clone() }
    }
    fn le_v(&self, other: &V) -> bool {
        match (self, other) {
            (V::S(a), V::S(b)) => *a <= *b,
            (V::S(a), V::B(b)) => BigInt::from(*a) <= *b,
            (V::B(a), V::S(b)) => *a <= BigInt::from(*b),
            (V::B(a), V::B(b)) => *a <= *b,
        }
    }
    fn lt_v(&self, other: &V) -> bool {
        match (self, other) {
            (V::S(a), V::S(b)) => *a < *b,
            (V::S(a), V::B(b)) => BigInt::from(*a) < *b,
            (V::B(a), V::S(b)) => *a < BigInt::from(*b),
            (V::B(a), V::B(b)) => *a < *b,
        }
    }
    fn clone_v(&self) -> V {
        match self { V::S(n) => V::S(*n), V::B(n) => V::B(n.clone()) }
    }
}
impl std::fmt::Display for V {
    fn fmt(&self, f: &mut std::fmt::Formatter) -> std::fmt::Result {
        match self { V::S(n) => write!(f, \"{}\"  , n), V::B(n) => write!(f, \"{}\"  , n) }
    }
}

// Shared arg buffer for BigInt-capable calls
static mut _SOMA_ARGS: Vec<V> = Vec::new();
static mut _SOMA_RESULT: Option<String> = None;

#[no_mangle]
pub extern \"C\" fn _soma_clear_args() {
    unsafe { _SOMA_ARGS.clear(); }
}

#[no_mangle]
pub extern \"C\" fn _soma_push_i64(v: i64) {
    unsafe { _SOMA_ARGS.push(V::S(v)); }
}

#[no_mangle]
pub extern \"C\" fn _soma_push_bigint(ptr: *const u8, len: i64) {
    unsafe {
        let bytes = std::slice::from_raw_parts(ptr, len as usize);
        let s = std::str::from_utf8_unchecked(bytes);
        let negative = s.starts_with('-');
        let digits = if negative { &s[1..] } else { s };
        let big: BigInt = digits.parse().unwrap();
        _SOMA_ARGS.push(V::B(if negative { -big } else { big }));
    }
}

#[no_mangle]
pub extern \"C\" fn _soma_result_ptr() -> *const u8 {
    unsafe { _SOMA_RESULT.as_ref().map(|s| s.as_ptr()).unwrap_or(std::ptr::null()) }
}

#[no_mangle]
pub extern \"C\" fn _soma_result_len() -> i64 {
    unsafe { _SOMA_RESULT.as_ref().map(|s| s.len() as i64).unwrap_or(0) }
}
");
            } // end v_enum_emitted guard

            // Generate zero-arg function that reads from shared buffer
            out.push_str(&format!("#[no_mangle]\npub extern \"C\" fn {}() -> i64 {{\n", fn_name));

            // Read params from shared buffer
            for (i, p) in handler.params.iter().enumerate() {
                let ty = type_expr_to_native(&p.ty.node);
                if ty == NativeType::Int {
                    out.push_str(&format!("    let {} = unsafe {{ _SOMA_ARGS[{}].clone_v() }};\n", p.name, i));
                } else {
                    // Float/Bool params: read as i64 bits then interpret
                    out.push_str(&format!("    let {} = unsafe {{ match &_SOMA_ARGS[{}] {{ V::S(n) => *n as {}, _ => 0 as {} }} }};\n",
                        p.name, i, ty.rust_str(), ty.rust_str()));
                }
            }

            // Generate body with V type
            for stmt in &handler.body {
                let code = gen.gen_stmt(&stmt.node, 1);
                // Transform BigInt code → V code
                let code = code
                    .replace(": BigInt", ": V")
                    .replace("BigInt::from(", "V::S(");

                // Transform assignment arithmetic patterns:
                // x = ((&x) OP (&y));  →  x.OP_v(&y); (in-place)
                // x = ((&x) OP V::S(N));  →  x.OP(N); (in-place with literal)
                let code = transform_v_arithmetic(&code);
                out.push_str(&code);
            }
            {
                let last_is_return = handler.body.last()
                    .map(|s| matches!(s.node, Statement::Return { .. }))
                    .unwrap_or(false);
                if !last_is_return { out.push_str("    0i64\n"); }
            }
            out.push_str("}\n\n");

            // BigInt result is read via shared _soma_result_ptr/_soma_result_len
        } else {
            out.push_str(&format!("#[no_mangle]\npub extern \"C\" fn {}({}) -> {} {{\n",
                fn_name, param_str, ret_type.rust_str()));
            for stmt in &handler.body {
                out.push_str(&gen.gen_stmt(&stmt.node, 1));
            }
            let last_is_return = handler.body.last()
                .map(|s| matches!(s.node, Statement::Return { .. }))
                .unwrap_or(false);
            if !last_is_return {
                let default = match ret_type {
                    NativeType::Float => "    0.0f64\n",
                    NativeType::Bool => "    false\n",
                    _ => "    0i64\n",
                };
                out.push_str(default);
            }
            out.push_str("}\n\n");
        }

        // Generate parallel version if handler is in the parallel list
        let is_parallel = parallel.enabled && parallel.handlers.contains(&handler.signal_name);
        if is_parallel && !handler.params.is_empty() {
            let n_threads = if parallel.threads > 0 { parallel.threads } else { 0 };
            let threads_expr = if n_threads > 0 {
                format!("{}usize", n_threads)
            } else {
                "std::thread::available_parallelism().map(|n| n.get()).unwrap_or(1)".to_string()
            };

            // Parallel strategy: split the FIRST param (iteration count) across threads.
            // Each thread calls the sequential handler with chunk_size.
            // Results are averaged (weighted by chunk size).
            // This works for any Monte Carlo / accumulation handler.
            let first_param = &handler.params[0].name;
            let other_params: Vec<String> = handler.params[1..].iter()
                .map(|p| format!("{}: {}", p.name, type_expr_to_native(&p.ty.node).rust_str()))
                .collect();
            let other_args: Vec<String> = handler.params[1..].iter()
                .map(|p| p.name.clone())
                .collect();

            let full_param_str: String = handler.params.iter()
                .map(|p| format!("{}: {}", p.name, type_expr_to_native(&p.ty.node).rust_str()))
                .collect::<Vec<_>>()
                .join(", ");

            let other_args_str = if other_args.is_empty() {
                String::new()
            } else {
                format!(", {}", other_args.join(", "))
            };

            out.push_str(&format!(
                "#[no_mangle]\npub extern \"C\" fn {fn_name}_par({full_param_str}) -> {} {{\n\
                \tlet _n_threads = {threads_expr};\n\
                \tlet _total = {first_param} as usize;\n\
                \tif _total < _n_threads * 1000 {{\n\
                \t\treturn {fn_name}({first_param}{other_args_str});\n\
                \t}}\n\
                \tlet _chunk = (_total + _n_threads - 1) / _n_threads;\n\
                \tlet _result: f64 = std::thread::scope(|_s| {{\n\
                \t\tlet _handles: Vec<_> = (0.._n_threads).map(|_t| {{\n\
                \t\t\tlet _start = _t * _chunk;\n\
                \t\t\tlet _end = std::cmp::min((_t + 1) * _chunk, _total);\n\
                \t\t\tlet _chunk_n = (_end - _start) as i64;\n\
                \t\t\t_s.spawn(move || {{\n\
                \t\t\t\t{fn_name}(_chunk_n{other_args_str}) * _chunk_n as f64\n\
                \t\t\t}})\n\
                \t\t}}).collect();\n\
                \t\t_handles.into_iter().map(|h| h.join().unwrap()).sum::<f64>()\n\
                \t}});\n\
                \t(_result / {first_param} as f64) as {}\n\
                }}\n\n",
                ret_type.rust_str(), ret_type.rust_str()
            ));
        }

        let param_types: Vec<NativeType> = handler.params.iter()
            .map(|p| type_expr_to_native(&p.ty.node))
            .collect();

        // Use parallel version if available
        if is_parallel {
            if param_types.len() > 3 {
                // Generate array wrapper for the parallel version
                out.push_str(&format!("#[no_mangle]\npub extern \"C\" fn {}_par_arr(args: *const f64, _count: i64) -> f64 {{\n", fn_name));
                out.push_str("    unsafe {\n");
                let call_args: Vec<String> = handler.params.iter().enumerate().map(|(i, p)| {
                    let ty = type_expr_to_native(&p.ty.node);
                    match ty {
                        NativeType::Int => format!("*args.add({}) as i64", i),
                        NativeType::Float => format!("*args.add({})", i),
                        NativeType::Bool => format!("*args.add({}) != 0.0", i),
                        NativeType::String => format!("*args.add({})", i),
                    }
                }).collect();
                out.push_str(&format!("        let r = {}_par({});\n", fn_name, call_args.join(", ")));
                out.push_str("        r as f64\n    }\n}\n\n");
                sigs.push(NativeSig {
                    fn_name: format!("{}_par_arr", fn_name),
                    param_types: param_types.clone(),
                    return_type: ret_type,
                    uses_shared_args: false,
                });
            } else {
                sigs.push(NativeSig {
                    fn_name: format!("{}_par", fn_name),
                    param_types: param_types.clone(),
                    return_type: ret_type,
                    uses_shared_args: false,
                });
            }
        } else
        // For >3 params, generate an array-based wrapper
        if param_types.len() > 3 {
            out.push_str(&format!("#[no_mangle]\npub extern \"C\" fn {}_arr(args: *const f64, _count: i64) -> f64 {{\n", fn_name));
            out.push_str("    unsafe {\n");
            let call_args: Vec<String> = handler.params.iter().enumerate().map(|(i, p)| {
                let ty = type_expr_to_native(&p.ty.node);
                match ty {
                    NativeType::Int => format!("*args.add({}) as i64", i),
                    NativeType::Float => format!("*args.add({})", i),
                    NativeType::Bool => format!("*args.add({}) != 0.0", i),
                    NativeType::String => format!("*args.add({})", i),
                }
            }).collect();
            out.push_str(&format!("        let r = {}({});\n", fn_name, call_args.join(", ")));
            match ret_type {
                NativeType::Float => out.push_str("        r\n"),
                NativeType::Int => out.push_str("        r as f64\n"),
                NativeType::Bool => out.push_str("        if r { 1.0 } else { 0.0 }\n"),
                NativeType::String => out.push_str("        0.0\n"),
            }
            out.push_str("    }\n}\n\n");

            // Use the array wrapper as the exported symbol
            sigs.push(NativeSig {
                fn_name: format!("{}_arr", fn_name),
                param_types,
                return_type: ret_type,
                uses_shared_args: false,
            });
        } else {
            let shared = ret_type == NativeType::Int || ret_type == NativeType::String;
            sigs.push(NativeSig {
                fn_name,
                param_types,
                return_type: ret_type,
                uses_shared_args: shared,
            });
        }
    }

    (out, sigs)
}

fn type_expr_to_native(ty: &TypeExpr) -> NativeType {
    match ty {
        TypeExpr::Simple(name) => match name.as_str() {
            "Int" => NativeType::Int,
            "Float" => NativeType::Float,
            "Bool" => NativeType::Bool,
            "String" => NativeType::String,
            _ => NativeType::Float, // fallback
        },
        _ => NativeType::Float,
    }
}

struct FnGenerator {
    var_types: HashMap<String, NativeType>,
    siblings: HashSet<String>,
    fn_name_upper: Option<String>,
}

impl FnGenerator {
    fn new(params: &[Param], siblings: &HashSet<String>, fn_name: &str) -> Self {
        let mut var_types = HashMap::new();
        for p in params {
            var_types.insert(p.name.clone(), type_expr_to_native(&p.ty.node));
        }
        Self {
            var_types,
            siblings: siblings.clone(),
            fn_name_upper: Some(fn_name.to_uppercase()),
        }
    }

    fn infer_body_types(&mut self, body: &[Spanned<Statement>]) {
        for stmt in body {
            self.infer_stmt_types(&stmt.node);
        }
    }

    fn infer_stmt_types(&mut self, stmt: &Statement) {
        match stmt {
            Statement::Let { name, value } => {
                let ty = self.infer_expr_type(&value.node);
                self.var_types.insert(name.clone(), ty);
            }
            Statement::Assign { name, value } => {
                let ty = self.infer_expr_type(&value.node);
                // Assignment can promote int to float
                if let Some(existing) = self.var_types.get(name) {
                    if *existing == NativeType::Int && ty == NativeType::Float {
                        self.var_types.insert(name.clone(), NativeType::Float);
                    }
                } else {
                    self.var_types.insert(name.clone(), ty);
                }
            }
            Statement::If { then_body, else_body, .. } => {
                for s in then_body { self.infer_stmt_types(&s.node); }
                for s in else_body { self.infer_stmt_types(&s.node); }
            }
            Statement::While { body, .. } => {
                for s in body { self.infer_stmt_types(&s.node); }
            }
            Statement::For { var, iter, body, .. } => {
                // for var in range(...) → var is Int
                self.var_types.insert(var.clone(), NativeType::Int);
                let _ = self.infer_expr_type(&iter.node);
                for s in body { self.infer_stmt_types(&s.node); }
            }
            _ => {}
        }
    }

    fn infer_expr_type(&self, expr: &Expr) -> NativeType {
        match expr {
            Expr::Literal(Literal::Int(_)) => NativeType::Int,
            Expr::Literal(Literal::Float(_)) => NativeType::Float,
            Expr::Literal(Literal::Bool(_)) => NativeType::Bool,
            Expr::Literal(Literal::String(_)) => NativeType::String,
            Expr::Ident(name) => {
                self.var_types.get(name).copied().unwrap_or(NativeType::Float)
            }
            Expr::BinaryOp { left, op, right } => {
                let lt = self.infer_expr_type(&left.node);
                let rt = self.infer_expr_type(&right.node);
                match op {
                    BinOp::Div => {
                        // Int/Int → Int (floor division), otherwise Float
                        if lt == NativeType::Int && rt == NativeType::Int {
                            NativeType::Int
                        } else {
                            NativeType::Float
                        }
                    }
                    BinOp::And | BinOp::Or => NativeType::Bool,
                    BinOp::Add if lt == NativeType::String || rt == NativeType::String => NativeType::String,
                    _ => {
                        if lt == NativeType::Float || rt == NativeType::Float {
                            NativeType::Float
                        } else {
                            lt // both Int → Int, both Bool → Bool
                        }
                    }
                }
            }
            Expr::CmpOp { .. } => NativeType::Bool,
            Expr::Not(_) => NativeType::Bool,
            Expr::FnCall { name, .. } => {
                match name.as_str() {
                    "random" | "sqrt" | "log" | "exp" | "pow" | "sin" | "cos" => NativeType::Float,
                    "to_string" => NativeType::String,
                    "len" | "floor" | "ceil" | "round" => NativeType::Int,
                    "abs" | "min" | "max" => {
                        // Depends on args, default to float
                        NativeType::Float
                    }
                    _ => NativeType::Float,
                }
            }
            _ => NativeType::Float,
        }
    }

    fn infer_return_type(&self, body: &[Spanned<Statement>]) -> NativeType {
        // Find the first return statement and infer from its expression
        for stmt in body {
            match &stmt.node {
                Statement::Return { value } => {
                    return self.infer_expr_type(&value.node);
                }
                Statement::If { then_body, else_body, .. } => {
                    if let Some(ty) = self.find_return_type(then_body) {
                        return ty;
                    }
                    if let Some(ty) = self.find_return_type(else_body) {
                        return ty;
                    }
                }
                _ => {}
            }
        }
        // No explicit return — check last expression
        if let Some(last) = body.last() {
            if let Statement::ExprStmt { expr } = &last.node {
                return self.infer_expr_type(&expr.node);
            }
        }
        NativeType::Float // default
    }

    fn find_return_type(&self, body: &[Spanned<Statement>]) -> Option<NativeType> {
        for stmt in body {
            if let Statement::Return { value } = &stmt.node {
                return Some(self.infer_expr_type(&value.node));
            }
        }
        None
    }

    fn indent(level: usize) -> String {
        "    ".repeat(level)
    }

    fn gen_stmt(&self, stmt: &Statement, indent: usize) -> String {
        let ind = Self::indent(indent);
        match stmt {
            Statement::Let { name, value } => {
                let ty = self.var_types.get(name).copied().unwrap_or(NativeType::Float);
                let expr = self.gen_expr(&value.node, ty);
                format!("{}let mut {}: {} = {};\n", ind, name, ty.compute_str(), expr)
            }
            Statement::Assign { name, value } => {
                let ty = self.var_types.get(name).copied().unwrap_or(NativeType::Float);
                let expr = self.gen_expr(&value.node, ty);
                format!("{}{} = {};\n", ind, name, expr)
            }
            Statement::Return { value } => {
                let ret_ty = self.infer_expr_type(&value.node);
                let expr = self.gen_expr(&value.node, ret_ty);
                if ret_ty == NativeType::Int {
                    let upper = self.fn_name_upper.as_deref().unwrap_or("UNKNOWN");
                    let mut s = String::new();
                    s.push_str(&format!("{}let _ret_val = {};\n", ind, expr));
                    s.push_str(&format!("{}use num_traits::ToPrimitive;\n", ind));
                    s.push_str(&format!("{}match _ret_val.to_i64() {{\n", ind));
                    s.push_str(&format!("{}    Some(v) => return v,\n", ind));
                    s.push_str(&format!("{}    None => {{\n", ind));
                    s.push_str(&format!("{}        unsafe {{ _SOMA_RESULT = Some(_ret_val.to_string()); }}\n", ind));
                    s.push_str(&format!("{}        return i64::MIN;\n", ind));
                    s.push_str(&format!("{}    }}\n", ind));
                    s.push_str(&format!("{}}}\n", ind));
                    s
                } else if ret_ty == NativeType::String {
                    let mut s = String::new();
                    s.push_str(&format!("{}unsafe {{ _SOMA_RESULT = Some({}); }}\n", ind, expr));
                    s.push_str(&format!("{}return i64::MIN + 1;\n", ind));
                    s
                } else {
                    format!("{}return {};\n", ind, expr)
                }
            }
            Statement::If { condition, then_body, else_body } => {
                let cond = self.gen_expr(&condition.node, NativeType::Bool);
                let mut s = format!("{}if {} {{\n", ind, cond);
                for st in then_body {
                    s.push_str(&self.gen_stmt(&st.node, indent + 1));
                }
                if else_body.is_empty() {
                    s.push_str(&format!("{}}}\n", ind));
                } else {
                    s.push_str(&format!("{}}} else {{\n", ind));
                    for st in else_body {
                        s.push_str(&self.gen_stmt(&st.node, indent + 1));
                    }
                    s.push_str(&format!("{}}}\n", ind));
                }
                s
            }
            Statement::While { condition, body } => {
                let cond = self.gen_expr(&condition.node, NativeType::Bool);
                let mut s = format!("{}while {} {{\n", ind, cond);
                for st in body {
                    s.push_str(&self.gen_stmt(&st.node, indent + 1));
                }
                s.push_str(&format!("{}}}\n", ind));
                s
            }
            Statement::For { var, iter, body } => {
                let iter_code = self.gen_for_iter(&iter.node);
                let mut s = format!("{}for {} in {} {{\n", ind, var, iter_code);
                for st in body {
                    s.push_str(&self.gen_stmt(&st.node, indent + 1));
                }
                s.push_str(&format!("{}}}\n", ind));
                s
            }
            Statement::Break => format!("{}break;\n", ind),
            Statement::Continue => format!("{}continue;\n", ind),
            Statement::ExprStmt { expr } => {
                let code = self.gen_expr(&expr.node, NativeType::Float);
                format!("{}{};\n", ind, code)
            }
            _ => format!("{}// unsupported statement\n", ind),
        }
    }

    fn gen_for_iter(&self, expr: &Expr) -> String {
        // range(a, b) → a..b
        if let Expr::FnCall { name, args } = expr {
            if name == "range" && args.len() == 2 {
                let a = self.gen_expr(&args[0].node, NativeType::Int);
                let b = self.gen_expr(&args[1].node, NativeType::Int);
                return format!("{}..{}", a, b);
            }
        }
        // Fallback
        format!("0..0 /* unsupported iterator */")
    }

    fn gen_expr(&self, expr: &Expr, target_ty: NativeType) -> String {
        match expr {
            Expr::Literal(Literal::Int(n)) => {
                if target_ty == NativeType::Float {
                    format!("{}.0f64", n)
                } else {
                    format!("BigInt::from({}i64)", n)
                }
            }
            Expr::Literal(Literal::Float(f)) => format!("{}f64", f),
            Expr::Literal(Literal::Bool(b)) => format!("{}", b),
            Expr::Literal(Literal::String(s)) => format!("\"{}\".to_string()", s.replace('\\', "\\\\").replace('"', "\\\"")),
            Expr::Ident(name) => {
                let var_ty = self.var_types.get(name).copied().unwrap_or(NativeType::Float);
                if var_ty == NativeType::String {
                    format!("{}.clone()", name)
                } else if target_ty == NativeType::Float && var_ty == NativeType::Int {
                    format!("{}.to_i64().unwrap_or(0) as f64", name)
                } else if target_ty == NativeType::Int && var_ty == NativeType::Float {
                    format!("BigInt::from({} as i64)", name)
                } else if var_ty == NativeType::Int {
                    // Use reference to avoid cloning BigInt
                    format!("(&{})", name)
                } else {
                    name.clone()
                }
            }
            Expr::BinaryOp { left, op, right } => {
                let result_ty = self.infer_expr_type(expr);
                let lt = self.infer_expr_type(&left.node);
                let rt = self.infer_expr_type(&right.node);

                match op {
                    BinOp::Div => {
                        if lt == NativeType::Int && rt == NativeType::Int {
                            // Integer division via V type (BigInt-safe)
                            let l = self.gen_expr(&left.node, NativeType::Int);
                            let r = self.gen_expr(&right.node, NativeType::Int);
                            format!("({} / {})", l, r)
                        } else {
                            // Float division
                            let l = self.gen_expr(&left.node, NativeType::Float);
                            let r = self.gen_expr(&right.node, NativeType::Float);
                            let inner = format!("({} / {})", l, r);
                            if target_ty == NativeType::Int {
                                format!("({} as i128)", inner)
                            } else {
                                inner
                            }
                        }
                    }
                    BinOp::Mod => {
                        // Modulo: keep types consistent
                        let common = if lt == NativeType::Float || rt == NativeType::Float {
                            NativeType::Float
                        } else {
                            NativeType::Int
                        };
                        let l = self.gen_expr(&left.node, common);
                        let r = self.gen_expr(&right.node, common);
                        format!("({} % {})", l, r)
                    }
                    BinOp::And => {
                        let l = self.gen_expr(&left.node, NativeType::Bool);
                        let r = self.gen_expr(&right.node, NativeType::Bool);
                        format!("({} && {})", l, r)
                    }
                    BinOp::Or => {
                        let l = self.gen_expr(&left.node, NativeType::Bool);
                        let r = self.gen_expr(&right.node, NativeType::Bool);
                        format!("({} || {})", l, r)
                    }
                    BinOp::Add if lt == NativeType::String || rt == NativeType::String => {
                        // String concatenation
                        let l = self.gen_expr(&left.node, NativeType::String);
                        let r = self.gen_expr(&right.node, NativeType::String);
                        format!("format!(\"{{}}{{}}\", {}, {})", l, r)
                    }
                    _ => {
                        let common = if lt == NativeType::Float || rt == NativeType::Float {
                            NativeType::Float
                        } else {
                            result_ty
                        };
                        let l = self.gen_expr(&left.node, common);
                        let r = self.gen_expr(&right.node, common);
                        let op_str = match op { BinOp::Add => "+", BinOp::Sub => "-", BinOp::Mul => "*", _ => unreachable!() };
                        let inner = format!("({} {} {})", l, op_str, r);
                        // Coerce to target if needed
                        if target_ty == NativeType::Float && common == NativeType::Int {
                            format!("({} as f64)", inner)
                        } else if target_ty == NativeType::Int && common == NativeType::Float {
                            format!("({} as i128)", inner)
                        } else {
                            inner
                        }
                    }
                }
            }
            Expr::CmpOp { left, op, right } => {
                // Compare in the wider type
                let lt = self.infer_expr_type(&left.node);
                let rt = self.infer_expr_type(&right.node);
                let common = if lt == NativeType::Float || rt == NativeType::Float {
                    NativeType::Float
                } else {
                    NativeType::Int
                };
                let l = self.gen_expr(&left.node, common);
                let r = self.gen_expr(&right.node, common);
                let op_str = match op {
                    CmpOp::Lt => "<",
                    CmpOp::Gt => ">",
                    CmpOp::Le => "<=",
                    CmpOp::Ge => ">=",
                    CmpOp::Eq => "==",
                    CmpOp::Ne => "!=",
                };
                format!("({} {} {})", l, op_str, r)
            }
            Expr::Not(inner) => {
                let e = self.gen_expr(&inner.node, NativeType::Bool);
                format!("(!{})", e)
            }
            Expr::FnCall { name, args } => {
                self.gen_fn_call(name, args, target_ty)
            }
            Expr::Pipe { left, right } => {
                self.gen_pipe(&left.node, &right.node, target_ty)
            }
            Expr::Lambda { param, body } => {
                // Inline lambda: |param| { body }
                let body_code = self.gen_expr(&body.node, NativeType::Float);
                format!("|{}| {{ {} }}", param, body_code)
            }
            Expr::LambdaBlock { param, stmts, result } => {
                let mut code = format!("|{}| {{\n", param);
                for stmt in stmts {
                    code.push_str(&self.gen_stmt(&stmt.node, 2));
                }
                code.push_str(&format!("        {}\n    }}", self.gen_expr(&result.node, NativeType::Float)));
                code
            }
            Expr::ListLiteral(elements) => {
                let items: Vec<String> = elements.iter().map(|e| self.gen_expr(&e.node, target_ty)).collect();
                format!("vec![{}]", items.join(", "))
            }
            Expr::FieldAccess { target, field } => {
                // For lambda params accessing map fields — in native, these are struct fields
                // For now, treat as tuple access or direct field
                let t = self.gen_expr(&target.node, target_ty);
                format!("{}.{}", t, field)
            }
            _ => "/* unsupported expr */ 0".to_string(),
        }
    }

    fn gen_fn_call(&self, name: &str, args: &[Spanned<Expr>], target_ty: NativeType) -> String {
        match name {
            "random" => {
                "unsafe { _soma_random() }".to_string()
            }
            "sqrt" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                format!("({}).sqrt()", a)
            }
            "log" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                format!("({}).ln()", a)
            }
            "exp" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                format!("({}).exp()", a)
            }
            "pow" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                let b = self.gen_expr(&args[1].node, NativeType::Float);
                format!("({}).powf({})", a, b)
            }
            "abs" => {
                let a_ty = self.infer_expr_type(&args[0].node);
                let a = self.gen_expr(&args[0].node, a_ty);
                format!("({}).abs()", a)
            }
            "min" => {
                if args.len() == 2 {
                    let a_ty = self.infer_expr_type(&args[0].node);
                    let b_ty = self.infer_expr_type(&args[1].node);
                    let common = if a_ty == NativeType::Float || b_ty == NativeType::Float {
                        NativeType::Float
                    } else {
                        NativeType::Int
                    };
                    let a = self.gen_expr(&args[0].node, common);
                    let b = self.gen_expr(&args[1].node, common);
                    if common == NativeType::Float {
                        format!("({}).min({})", a, b) // f64::min
                    } else {
                        format!("std::cmp::min({}, {})", a, b)
                    }
                } else {
                    "0".to_string()
                }
            }
            "max" => {
                if args.len() == 2 {
                    let a_ty = self.infer_expr_type(&args[0].node);
                    let b_ty = self.infer_expr_type(&args[1].node);
                    let common = if a_ty == NativeType::Float || b_ty == NativeType::Float {
                        NativeType::Float
                    } else {
                        NativeType::Int
                    };
                    let a = self.gen_expr(&args[0].node, common);
                    let b = self.gen_expr(&args[1].node, common);
                    if common == NativeType::Float {
                        format!("({}).max({})", a, b)
                    } else {
                        format!("std::cmp::max({}, {})", a, b)
                    }
                } else {
                    "0".to_string()
                }
            }
            "floor" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                format!("BigInt::from(({}).floor() as i64)", a)
            }
            "ceil" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                format!("BigInt::from(({}).ceil() as i64)", a)
            }
            "round" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                format!("BigInt::from(({}).round() as i64)", a)
            }
            "sin" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                format!("({}).sin()", a)
            }
            "cos" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                format!("({}).cos()", a)
            }
            "to_string" => {
                let arg_ty = self.infer_expr_type(&args[0].node);
                let a = self.gen_expr(&args[0].node, arg_ty);
                format!("format!(\"{{}}\", {})", a)
            }
            "len" => {
                let arg_ty = self.infer_expr_type(&args[0].node);
                if arg_ty == NativeType::String {
                    let a = self.gen_expr(&args[0].node, NativeType::String);
                    format!("BigInt::from({}.len() as i64)", a)
                } else {
                    let a = self.gen_expr(&args[0].node, NativeType::Int);
                    format!("BigInt::from(0i64)") // lists not yet supported
                }
            }
            "nth" => {
                format!("/* nth */ 0") // simplified
            }
            "range" => {
                // Used in for loops, shouldn't appear as expression
                if args.len() == 2 {
                    let a = self.gen_expr(&args[0].node, NativeType::Int);
                    let b = self.gen_expr(&args[1].node, NativeType::Int);
                    format!("({}..{})", a, b)
                } else {
                    "(0..0)".to_string()
                }
            }
            other => {
                // Call to another native handler in the same cell
                let arg_strs: Vec<String> = args.iter()
                    .map(|a| {
                        let ty = self.infer_expr_type(&a.node);
                        self.gen_expr(&a.node, ty)
                    })
                    .collect();
                format!("handler_{}({})", other, arg_strs.join(", "))
            }
        }
    }

    /// Generate parallel Rust code for pipe expressions.
    /// range(0, n) |> map(f) |> reduce(init, f) → parallel iteration
    /// Flatten a pipe chain into: (source, [(op_name, args), ...], optional_suffix_expr)
    fn flatten_pipe<'a>(&self, expr: &'a Expr) -> (String, Vec<(&'a str, &'a [Spanned<Expr>])>) {
        match expr {
            Expr::Pipe { left, right } => {
                let (source, mut ops) = self.flatten_pipe(&left.node);
                match &right.node {
                    Expr::FnCall { name, args } => {
                        ops.push((name.as_str(), args.as_slice()));
                    }
                    // Handle: pipe |> reduce(...) / n — arithmetic wrapping a pipe op
                    Expr::BinaryOp { left: inner_left, .. } => {
                        if let Expr::FnCall { name, args } = &inner_left.node {
                            ops.push((name.as_str(), args.as_slice()));
                        }
                        // The arithmetic (/ n * 4.0) will be handled by the BinaryOp in gen_expr
                        // since we only flatten the pipe part
                    }
                    _ => {}
                }
                (source, ops)
            }
            Expr::FnCall { name, args } if name == "range" && args.len() == 2 => {
                let a = self.gen_expr(&args[0].node, NativeType::Int);
                let b = self.gen_expr(&args[1].node, NativeType::Int);
                (format!("({}..{})", a, b), Vec::new())
            }
            other => (self.gen_expr(other, NativeType::Float), Vec::new()),
        }
    }

    fn gen_pipe(&self, left: &Expr, right: &Expr, _target_ty: NativeType) -> String {
        // Collect: pipe source + right-side operation
        let (source, mut ops) = self.flatten_pipe(left);

        // Extract the FnCall from right — it may be wrapped in nested BinaryOps
        // e.g., reduce(0.0, f) / n * 4.0 → BinaryOp(BinaryOp(reduce, Div, n), Mul, 4.0)
        let mut suffix = String::new();
        let mut current = right;
        // Peel off BinaryOps from outside-in, collecting suffix operations
        let mut suffix_ops: Vec<(BinOp, String)> = Vec::new();
        loop {
            match current {
                Expr::FnCall { name, args } => {
                    ops.push((name.as_str(), args.as_slice()));
                    break;
                }
                Expr::BinaryOp { left: inner, op, right: rhs } => {
                    let op_str = match op {
                        BinOp::Add => "+", BinOp::Sub => "-", BinOp::Mul => "*",
                        BinOp::Div => "/", BinOp::Mod => "%", _ => "+",
                    };
                    let rhs_code = self.gen_expr(&rhs.node, NativeType::Float);
                    suffix_ops.push((*op, rhs_code));
                    current = &inner.node;
                }
                _ => break,
            }
        }
        // Build suffix from innermost to outermost
        for (op, rhs) in suffix_ops.iter().rev() {
            let op_str = match op {
                BinOp::Add => "+", BinOp::Sub => "-", BinOp::Mul => "*",
                BinOp::Div => "/", BinOp::Mod => "%", _ => "+",
            };
            suffix = format!("{} {} {}", suffix, op_str, rhs);
        }

        // Build a Rust iterator chain
        let mut chain = source;

        for (op_name, args) in &ops {
            match *op_name {
                "map" if args.len() == 1 => {
                    let lambda_body = self.gen_map_lambda(&args[0].node);
                    // Extract the lambda param name
                    let param_name = match &args[0].node {
                        Expr::Lambda { param, .. } | Expr::LambdaBlock { param, .. } => param.clone(),
                        _ => "_i".to_string(),
                    };
                    chain = format!("({}).map(|{}| -> f64 {{ {} }})", chain, param_name, lambda_body);
                }
                "filter" if args.len() == 1 => {
                    let lambda_body = self.gen_expr(&args[0].node, NativeType::Bool);
                    chain = format!("({}).filter({})", chain, lambda_body);
                }
                "reduce" | "fold" if args.len() == 2 => {
                    let mut init = self.gen_expr(&args[0].node, NativeType::Float);
                    // Avoid double suffix like 0f64f64
                    if !init.ends_with("f64") { init = format!("{}f64", init); }
                    let fold_body = self.gen_reduce_lambda(&args[1].node);
                    chain = format!("({}).fold({}, |_acc, _val| {{ let _val = _val as f64; {} }})", chain, init, fold_body);
                }
                _ => {
                    chain = format!("/* unknown pipe op: {} */ {}", op_name, chain);
                }
            }
        }

        // Check if the chain ends with a fold (returns f64) or a map (returns iterator)
        let last_op = ops.last().map(|(name, _)| *name).unwrap_or("");
        if last_op != "reduce" && last_op != "fold" {
            chain = format!("({}).collect::<Vec<f64>>()", chain);
        }

        // Apply suffix arithmetic (e.g., / n * 4.0)
        if !suffix.is_empty() {
            chain = format!("({}{})", chain, suffix);
        }

        chain
    }

    /// Generate the body of a map lambda for native pipes.
    /// Handles both simple lambdas and block lambdas.
    fn gen_map_lambda(&self, expr: &Expr) -> String {
        match expr {
            Expr::Lambda { param: _, body } => {
                // The lambda body — generate as Rust expression
                self.gen_map_lambda_body(&body.node)
            }
            Expr::LambdaBlock { param: _, stmts, result } => {
                let mut code = String::new();
                for stmt in stmts {
                    code.push_str(&self.gen_stmt(&stmt.node, 3));
                }
                code.push_str(&format!("{}", self.gen_map_lambda_body(&result.node)));
                code
            }
            _ => "0.0f64".to_string(),
        }
    }

    fn gen_map_lambda_body(&self, expr: &Expr) -> String {
        match expr {
            Expr::FnCall { name, args } => {
                // Call to another native handler or builtin
                self.gen_fn_call(name, args, NativeType::Float)
            }
            Expr::BinaryOp { left, op, right } => {
                let l = self.gen_map_lambda_body(&left.node);
                let r = self.gen_map_lambda_body(&right.node);
                let op_str = match op {
                    BinOp::Add => "+", BinOp::Sub => "-", BinOp::Mul => "*",
                    BinOp::Div => "/", BinOp::Mod => "%", _ => "+",
                };
                format!("({} {} {})", l, op_str, r)
            }
            Expr::Literal(Literal::Float(f)) => format!("{}f64", f),
            Expr::Literal(Literal::Int(n)) => format!("{}f64", n),
            Expr::Ident(name) => format!("{} as f64", name),
            _ => self.gen_expr(expr, NativeType::Float),
        }
    }

    /// Translate a reduce lambda (p => p.acc + p.val) into Rust fold body
    /// The lambda parameter accesses .acc and .val — we replace with _acc and _val
    fn gen_reduce_lambda(&self, expr: &Expr) -> String {
        match expr {
            Expr::Lambda { param, body } => {
                // Replace param.acc → _acc, param.val → _val in the body
                self.gen_reduce_body(&body.node, param)
            }
            Expr::LambdaBlock { param, stmts: _, result } => {
                self.gen_reduce_body(&result.node, param)
            }
            _ => "_acc + _val".to_string(),
        }
    }

    fn gen_reduce_body(&self, expr: &Expr, param: &str) -> String {
        match expr {
            Expr::BinaryOp { left, op, right } => {
                let l = self.gen_reduce_body(&left.node, param);
                let r = self.gen_reduce_body(&right.node, param);
                let op_str = match op {
                    BinOp::Add => "+",
                    BinOp::Sub => "-",
                    BinOp::Mul => "*",
                    BinOp::Div => "/",
                    BinOp::Mod => "%",
                    _ => "+",
                };
                format!("({} {} {})", l, op_str, r)
            }
            Expr::FieldAccess { target, field } => {
                if let Expr::Ident(name) = &target.node {
                    if name == param {
                        match field.as_str() {
                            "acc" => "_acc".to_string(),
                            "val" => "_val".to_string(),
                            _ => format!("/* unknown field {} */", field),
                        }
                    } else {
                        format!("{}.{}", name, field)
                    }
                } else {
                    "_acc".to_string()
                }
            }
            Expr::Ident(name) => name.clone(),
            Expr::Literal(Literal::Int(n)) => format!("{}i64 as f64", n),
            Expr::Literal(Literal::Float(f)) => format!("{}f64", f),
            Expr::FnCall { name, args } => {
                self.gen_fn_call(name, args, NativeType::Float)
            }
            _ => "_acc + _val".to_string(),
        }
    }
}

// ── Rug mode: direct rug::Integer codegen (no V enum) ───────────────

/// Determine if a handler should use Rug mode (direct rug::Integer).
/// Criteria: returns Int or String, has Int variables, AND has a while loop.
/// Float-returning handlers stay in V mode even if they use Int counters.
fn should_use_rug(handler: &NativeHandler, gen: &FnGenerator) -> bool {
    let ret_type = gen.infer_return_type(&handler.body);
    // Only use rug for Int-returning or String-returning handlers
    if ret_type != NativeType::Int && ret_type != NativeType::String {
        return false;
    }
    let has_int = handler.params.iter().any(|p| type_expr_to_native(&p.ty.node) == NativeType::Int)
        || gen.var_types.values().any(|t| *t == NativeType::Int);
    if !has_int { return false; }
    let has_while = handler.body.iter().any(|s| matches!(s.node, Statement::While { .. }));
    has_while
}

impl FnGenerator {
    /// Generate a Rug-mode statement: all Int vars are rug::Integer.
    fn gen_stmt_rug(&self, stmt: &Statement, indent: usize) -> String {
        let ind = Self::indent(indent);
        match stmt {
            Statement::Let { name, value } => {
                let ty = self.var_types.get(name).copied().unwrap_or(NativeType::Float);
                match ty {
                    NativeType::Int => {
                        let expr = self.gen_expr_rug(&value.node);
                        format!("{}let mut {}: Integer = {};\n", ind, name, expr)
                    }
                    NativeType::String => {
                        let expr = self.gen_expr_rug_string(&value.node);
                        format!("{}let mut {}: String = {};\n", ind, name, expr)
                    }
                    NativeType::Float => {
                        let expr = self.gen_expr(&value.node, NativeType::Float);
                        format!("{}let mut {}: f64 = {};\n", ind, name, expr)
                    }
                    NativeType::Bool => {
                        let expr = self.gen_expr(&value.node, NativeType::Bool);
                        format!("{}let mut {}: bool = {};\n", ind, name, expr)
                    }
                }
            }
            Statement::Assign { name, value } => {
                let ty = self.var_types.get(name).copied().unwrap_or(NativeType::Float);
                match ty {
                    NativeType::Int => {
                        // Generate efficient in-place assignment
                        self.gen_assign_rug(name, &value.node, &ind)
                    }
                    NativeType::String => {
                        // Optimize: name = name + expr → name.push_str(expr)
                        if let Expr::BinaryOp { left, op: BinOp::Add, right } = &value.node {
                            if let Expr::Ident(ref lname) = left.node {
                                if lname == name {
                                    let rhs = self.gen_expr_rug_string(&right.node);
                                    return format!("{}{}.push_str(&{});\n", ind, name, rhs);
                                }
                            }
                        }
                        let expr = self.gen_expr_rug_string(&value.node);
                        format!("{}{} = {};\n", ind, name, expr)
                    }
                    _ => {
                        let expr = self.gen_expr(&value.node, ty);
                        format!("{}{} = {};\n", ind, name, expr)
                    }
                }
            }
            Statement::Return { value } => {
                let ret_ty = self.infer_expr_type(&value.node);
                if ret_ty == NativeType::Int {
                    let expr = self.gen_expr_rug(&value.node);
                    let mut s = String::new();
                    s.push_str(&format!("{}{{\n", ind));
                    s.push_str(&format!("{}    let _ret_val: Integer = {};\n", ind, expr));
                    s.push_str(&format!("{}    if let Some(v) = _ret_val.to_i64() {{\n", ind));
                    s.push_str(&format!("{}        return v;\n", ind));
                    s.push_str(&format!("{}    }} else {{\n", ind));
                    s.push_str(&format!("{}        unsafe {{ _SOMA_RESULT = Some(_ret_val.to_string()); }}\n", ind));
                    s.push_str(&format!("{}        return i64::MIN;\n", ind));
                    s.push_str(&format!("{}    }}\n", ind));
                    s.push_str(&format!("{}}}\n", ind));
                    s
                } else if ret_ty == NativeType::String {
                    let expr = self.gen_expr_rug_string(&value.node);
                    let mut s = String::new();
                    s.push_str(&format!("{}unsafe {{ _SOMA_RESULT = Some({}); }}\n", ind, expr));
                    s.push_str(&format!("{}return i64::MIN + 1;\n", ind));
                    s
                } else {
                    let expr = self.gen_expr(&value.node, ret_ty);
                    format!("{}return {};\n", ind, expr)
                }
            }
            Statement::If { condition, then_body, else_body } => {
                let cond = self.gen_cond_rug(&condition.node);
                let mut s = format!("{}if {} {{\n", ind, cond);
                for st in then_body {
                    s.push_str(&self.gen_stmt_rug(&st.node, indent + 1));
                }
                if else_body.is_empty() {
                    s.push_str(&format!("{}}}\n", ind));
                } else {
                    s.push_str(&format!("{}}} else {{\n", ind));
                    for st in else_body {
                        s.push_str(&self.gen_stmt_rug(&st.node, indent + 1));
                    }
                    s.push_str(&format!("{}}}\n", ind));
                }
                s
            }
            Statement::While { condition, body } => {
                let cond = self.gen_cond_rug(&condition.node);
                let mut s = String::new();

                // Hoist loop-internal Integer variables before the loop
                // so GMP can reuse their internal buffers across iterations
                let mut hoisted: HashSet<String> = HashSet::new();
                self.collect_loop_int_vars(body, &mut hoisted);
                for var in &hoisted {
                    s.push_str(&format!("{}let mut {}: Integer = Integer::new();\n", ind, var));
                }

                s.push_str(&format!("{}while {} {{\n", ind, cond));
                for st in body {
                    s.push_str(&self.gen_stmt_rug_hoisted(&st.node, indent + 1, &hoisted));
                }
                s.push_str(&format!("{}}}\n", ind));
                s
            }
            Statement::For { var, iter, body } => {
                let iter_code = self.gen_for_iter_rug(&iter.node);
                let mut s = format!("{}for {} in {} {{\n", ind, var, iter_code);
                for st in body {
                    s.push_str(&self.gen_stmt_rug(&st.node, indent + 1));
                }
                s.push_str(&format!("{}}}\n", ind));
                s
            }
            Statement::Break => format!("{}break;\n", ind),
            Statement::Continue => format!("{}continue;\n", ind),
            Statement::ExprStmt { expr } => {
                let code = self.gen_expr_rug(&expr.node);
                format!("{}{};\n", ind, code)
            }
            _ => format!("{}// unsupported statement\n", ind),
        }
    }

    /// Collect Integer variable names declared inside a loop body (for hoisting).
    fn collect_loop_int_vars(&self, body: &[Spanned<Statement>], vars: &mut HashSet<String>) {
        for stmt in body {
            match &stmt.node {
                Statement::Let { name, .. } => {
                    let ty = self.var_types.get(name).copied().unwrap_or(NativeType::Float);
                    if ty == NativeType::Int {
                        vars.insert(name.clone());
                    }
                }
                Statement::If { then_body, else_body, .. } => {
                    self.collect_loop_int_vars(then_body, vars);
                    self.collect_loop_int_vars(else_body, vars);
                }
                _ => {}
            }
        }
    }

    /// Like gen_stmt_rug but converts hoisted Let bindings to assign().
    fn gen_stmt_rug_hoisted(&self, stmt: &Statement, indent: usize, hoisted: &HashSet<String>) -> String {
        match stmt {
            Statement::Let { name, value } => {
                let ty = self.var_types.get(name).copied().unwrap_or(NativeType::Float);
                if ty == NativeType::Int && hoisted.contains(name) {
                    // Variable already declared before the loop — use assign
                    let ind = Self::indent(indent);
                    return self.gen_assign_rug(name, &value.node, &ind);
                }
                // Non-hoisted: delegate to normal
                self.gen_stmt_rug(stmt, indent)
            }
            Statement::If { condition, then_body, else_body } => {
                let ind = Self::indent(indent);
                let cond = self.gen_cond_rug(&condition.node);
                let mut s = format!("{}if {} {{\n", ind, cond);
                for st in then_body {
                    s.push_str(&self.gen_stmt_rug_hoisted(&st.node, indent + 1, hoisted));
                }
                if else_body.is_empty() {
                    s.push_str(&format!("{}}}\n", ind));
                } else {
                    s.push_str(&format!("{}}} else {{\n", ind));
                    for st in else_body {
                        s.push_str(&self.gen_stmt_rug_hoisted(&st.node, indent + 1, hoisted));
                    }
                    s.push_str(&format!("{}}}\n", ind));
                }
                s
            }
            _ => self.gen_stmt_rug(stmt, indent),
        }
    }

    /// Generate a Rug-mode expression that yields an `Integer`.
    fn gen_expr_rug(&self, expr: &Expr) -> String {
        match expr {
            Expr::Literal(Literal::Int(n)) => {
                format!("Integer::from({}i64)", n)
            }
            Expr::Literal(Literal::BigInt(s)) => {
                format!("Integer::from_str_radix(\"{}\", 10).unwrap()", s)
            }
            Expr::Ident(name) => {
                let var_ty = self.var_types.get(name).copied().unwrap_or(NativeType::Float);
                if var_ty == NativeType::Int {
                    // Clone the Integer
                    format!("{}.clone()", name)
                } else if var_ty == NativeType::Float {
                    format!("Integer::from({} as i64)", name)
                } else {
                    name.clone()
                }
            }
            Expr::BinaryOp { left, op, right } => {
                let lt = self.infer_expr_type(&left.node);
                let rt = self.infer_expr_type(&right.node);

                // If both sides are Int, generate Integer arithmetic
                if lt == NativeType::Int && rt == NativeType::Int {
                    self.gen_binop_rug(&left.node, *op, &right.node)
                } else if lt == NativeType::Float || rt == NativeType::Float {
                    // Mixed: compute in float, convert to Integer
                    let l = self.gen_expr(&left.node, NativeType::Float);
                    let r = self.gen_expr(&right.node, NativeType::Float);
                    let op_str = match op {
                        BinOp::Add => "+", BinOp::Sub => "-", BinOp::Mul => "*",
                        BinOp::Div => "/", BinOp::Mod => "%", _ => "+",
                    };
                    format!("Integer::from(({} {} {}) as i64)", l, op_str, r)
                } else {
                    // Fallback
                    let l = self.gen_expr_rug(&left.node);
                    let r = self.gen_expr_rug(&right.node);
                    format!("Integer::from(&{} + &{})", l, r)
                }
            }
            Expr::FnCall { name, args } => {
                self.gen_fn_call_rug(name, args)
            }
            Expr::Not(inner) => {
                let e = self.gen_cond_rug(&inner.node);
                format!("(!{})", e)
            }
            _ => format!("Integer::from(0i64) /* unsupported rug expr */")
        }
    }

    /// Generate an expression as an Incomplete value (no Integer::from() wrapper).
    /// Suitable for Assign trait: x.assign(incomplete_expr).
    fn gen_expr_rug_incomplete(&self, expr: &Expr) -> String {
        match expr {
            Expr::Literal(Literal::Int(n)) => format!("{}i64", n),
            Expr::BinaryOp { left, op, right } => {
                let lt = self.infer_expr_type(&left.node);
                let rt = self.infer_expr_type(&right.node);
                if lt == NativeType::Int && rt == NativeType::Int {
                    // Generate bare op expression (returns Incomplete)
                    let l = self.gen_expr_rug_ref(&left.node);
                    let r = self.gen_expr_rug_ref(&right.node);
                    let op_str = match op {
                        BinOp::Add => "+", BinOp::Sub => "-", BinOp::Mul => "*",
                        BinOp::Div => "/", BinOp::Mod => "%", _ => "+",
                    };
                    format!("{} {} {}", l, op_str, r)
                } else {
                    self.gen_expr_rug(expr)
                }
            }
            _ => self.gen_expr_rug(expr),
        }
    }

    /// Generate Integer binary operation.
    /// In rug, all &Integer ops return *Incomplete (lazy) — needs Integer::from().
    fn gen_binop_rug(&self, left: &Expr, op: BinOp, right: &Expr) -> String {
        let l = self.gen_expr_rug_ref(left);
        let r = self.gen_expr_rug_ref(right);
        let op_str = match op {
            BinOp::Add => "+", BinOp::Sub => "-", BinOp::Mul => "*",
            BinOp::Div => "/", BinOp::Mod => "%",
            _ => "+",
        };
        format!("Integer::from({} {} {})", l, op_str, r)
    }

    /// Generate a reference expression for use in Integer ops.
    /// Variables → &name, literals → i64 value (rug supports Integer op i64).
    fn gen_expr_rug_ref(&self, expr: &Expr) -> String {
        match expr {
            Expr::Literal(Literal::Int(n)) => {
                // Small literals: use as u32/i32 for efficient rug ops
                if *n >= 0 && *n <= u32::MAX as i64 {
                    format!("{}u32", n)
                } else if *n >= i32::MIN as i64 && *n <= i32::MAX as i64 {
                    format!("{}i32", n)
                } else {
                    format!("&Integer::from({}i64)", n)
                }
            }
            Expr::Ident(name) => {
                let var_ty = self.var_types.get(name).copied().unwrap_or(NativeType::Float);
                if var_ty == NativeType::Int {
                    format!("&{}", name)
                } else if var_ty == NativeType::Float {
                    format!("{} as i32", name)
                } else {
                    name.clone()
                }
            }
            Expr::BinaryOp { left, op, right } => {
                // Compound expression: generate as full Integer, then reference
                let inner = self.gen_binop_rug(&left.node, *op, &right.node);
                format!("&{}", inner)
            }
            Expr::FnCall { name, args } => {
                let inner = self.gen_fn_call_rug(name, args);
                format!("&{}", inner)
            }
            _ => {
                let inner = self.gen_expr_rug(expr);
                format!("&{}", inner)
            }
        }
    }

    /// Generate efficient in-place assignment for Integer variables.
    /// Tries to use in-place ops (+=, -=, *=, /=) when LHS appears in RHS.
    fn gen_assign_rug(&self, name: &str, value: &Expr, ind: &str) -> String {
        // Pattern: name = name OP expr  →  name OP= expr (in-place)
        if let Expr::BinaryOp { left, op, right } = value {
            // Check if left is `name`
            if let Expr::Ident(ref lname) = left.node {
                if lname == name {
                    let op_assign = match op {
                        BinOp::Add => "+=", BinOp::Sub => "-=", BinOp::Mul => "*=",
                        BinOp::Div => "/=", BinOp::Mod => "%=",
                        _ => "",
                    };
                    if !op_assign.is_empty() {
                        let r = self.gen_expr_rug_ref(&right.node);
                        return format!("{}{} {} {};\n", ind, name, op_assign, r);
                    }
                }
            }
            // Check if right is `name` and op is commutative
            if let Expr::Ident(ref rname) = right.node {
                if rname == name && matches!(op, BinOp::Add | BinOp::Mul) {
                    let op_assign = match op {
                        BinOp::Add => "+=", BinOp::Mul => "*=", _ => "",
                    };
                    if !op_assign.is_empty() {
                        let l = self.gen_expr_rug_ref(&left.node);
                        return format!("{}{} {} {};\n", ind, name, op_assign, l);
                    }
                }
            }
        }

        // Literal assignment: use assign(N) directly (no allocation)
        if let Expr::Literal(Literal::Int(n)) = value {
            return format!("{}{}.assign({}i64);\n", ind, name, n);
        }

        // Simple ident → swap (O(1) pointer swap, src is about to go out of scope)
        if let Expr::Ident(ref src) = value {
            let var_ty = self.var_types.get(src).copied().unwrap_or(NativeType::Float);
            if var_ty == NativeType::Int && src != name {
                return format!("{}std::mem::swap(&mut {}, &mut {});\n", ind, name, src);
            }
        }

        // General case: compute expression and assign to existing Integer.
        // Use gen_expr_rug_incomplete() to avoid Integer::from() wrapper.
        let expr = self.gen_expr_rug_incomplete(value);
        format!("{}{}.assign({});\n", ind, name, expr)
    }

    /// Generate a Rug-mode condition expression (returns bool).
    fn gen_cond_rug(&self, expr: &Expr) -> String {
        match expr {
            Expr::CmpOp { left, op, right } => {
                let lt = self.infer_expr_type(&left.node);
                let rt = self.infer_expr_type(&right.node);
                if lt == NativeType::Int && rt == NativeType::Int {
                    let l = self.gen_cmp_operand_rug(&left.node);
                    let r = self.gen_cmp_operand_rug(&right.node);
                    let op_str = match op {
                        CmpOp::Lt => "<", CmpOp::Gt => ">", CmpOp::Le => "<=",
                        CmpOp::Ge => ">=", CmpOp::Eq => "==", CmpOp::Ne => "!=",
                    };
                    format!("{} {} {}", l, op_str, r)
                } else {
                    // Float comparison
                    let common = if lt == NativeType::Float || rt == NativeType::Float {
                        NativeType::Float
                    } else {
                        NativeType::Int
                    };
                    let l = self.gen_expr(&left.node, common);
                    let r = self.gen_expr(&right.node, common);
                    let op_str = match op {
                        CmpOp::Lt => "<", CmpOp::Gt => ">", CmpOp::Le => "<=",
                        CmpOp::Ge => ">=", CmpOp::Eq => "==", CmpOp::Ne => "!=",
                    };
                    format!("({} {} {})", l, op_str, r)
                }
            }
            Expr::Not(inner) => {
                let e = self.gen_cond_rug(&inner.node);
                format!("(!{})", e)
            }
            Expr::BinaryOp { left, op, right } if matches!(op, BinOp::And | BinOp::Or) => {
                let l = self.gen_cond_rug(&left.node);
                let r = self.gen_cond_rug(&right.node);
                let op_str = match op { BinOp::And => "&&", BinOp::Or => "||", _ => "&&" };
                format!("({} {} {})", l, op_str, r)
            }
            Expr::Literal(Literal::Bool(b)) => format!("{}", b),
            Expr::Ident(name) => name.clone(),
            _ => format!("true /* unsupported rug cond */")
        }
    }

    /// Generate a comparison operand in Rug mode.
    /// For variables: no & prefix (Integer supports direct comparison).
    /// For literals: use i32/u32 (Integer supports comparison with primitives).
    fn gen_cmp_operand_rug(&self, expr: &Expr) -> String {
        match expr {
            Expr::Literal(Literal::Int(n)) => {
                if *n >= 0 && *n <= i32::MAX as i64 {
                    format!("{}i32", n)
                } else {
                    format!("Integer::from({}i64)", n)
                }
            }
            Expr::Ident(name) => {
                let var_ty = self.var_types.get(name).copied().unwrap_or(NativeType::Float);
                if var_ty == NativeType::Int {
                    name.clone()
                } else {
                    name.clone()
                }
            }
            _ => {
                // Complex expression: compute and compare
                self.gen_expr_rug(expr)
            }
        }
    }

    /// Generate a String expression in Rug mode.
    fn gen_expr_rug_string(&self, expr: &Expr) -> String {
        match expr {
            Expr::Literal(Literal::String(s)) => {
                format!("\"{}\".to_string()", s.replace('\\', "\\\\").replace('"', "\\\""))
            }
            Expr::BinaryOp { left, op: BinOp::Add, right } => {
                let lt = self.infer_expr_type(&left.node);
                let rt = self.infer_expr_type(&right.node);
                if lt == NativeType::String && rt == NativeType::String {
                    // Optimize: result + to_string(d) → format!
                    let l = self.gen_expr_rug_string(&left.node);
                    let r = self.gen_expr_rug_string(&right.node);
                    format!("format!(\"{{}}{{}}\", {}, {})", l, r)
                } else {
                    let l = self.gen_expr_rug_string(&left.node);
                    let r = self.gen_expr_rug_string(&right.node);
                    format!("format!(\"{{}}{{}}\", {}, {})", l, r)
                }
            }
            Expr::FnCall { name, args } if name == "to_string" && args.len() == 1 => {
                let arg_ty = self.infer_expr_type(&args[0].node);
                if arg_ty == NativeType::Int {
                    // For Ident, use var.to_string() directly (no &)
                    if let Expr::Ident(ref vname) = args[0].node {
                        format!("{}.to_string()", vname)
                    } else {
                        let a = self.gen_expr_rug(&args[0].node);
                        format!("{}.to_string()", a)
                    }
                } else {
                    let a = self.gen_expr(&args[0].node, arg_ty);
                    format!("format!(\"{{}}\", {})", a)
                }
            }
            Expr::Ident(name) => {
                let var_ty = self.var_types.get(name).copied().unwrap_or(NativeType::Float);
                if var_ty == NativeType::String {
                    format!("{}.clone()", name)
                } else if var_ty == NativeType::Int {
                    format!("{}.to_string()", name)
                } else {
                    format!("format!(\"{{}}\", {})", name)
                }
            }
            _ => {
                // Fallback
                let inner = self.gen_expr_rug(expr);
                format!("{}.to_string()", inner)
            }
        }
    }

    /// Generate a for-loop iterator in Rug mode.
    fn gen_for_iter_rug(&self, expr: &Expr) -> String {
        if let Expr::FnCall { name, args } = expr {
            if name == "range" && args.len() == 2 {
                // Range with Integer bounds — use i64 range since loop vars are typically small
                let a = self.gen_expr_rug_i64(&args[0].node);
                let b = self.gen_expr_rug_i64(&args[1].node);
                return format!("{}..{}", a, b);
            }
        }
        format!("0..0 /* unsupported rug iterator */")
    }

    /// Generate an expression as i64 (for loop bounds, small values).
    fn gen_expr_rug_i64(&self, expr: &Expr) -> String {
        match expr {
            Expr::Literal(Literal::Int(n)) => format!("{}i64", n),
            Expr::Ident(name) => {
                let var_ty = self.var_types.get(name).copied().unwrap_or(NativeType::Float);
                if var_ty == NativeType::Int {
                    format!("{}.to_i64().unwrap()", name)
                } else {
                    format!("{} as i64", name)
                }
            }
            _ => {
                let e = self.gen_expr_rug(expr);
                format!("{}.to_i64().unwrap()", e)
            }
        }
    }

    /// Generate a function call that returns Integer in Rug mode.
    fn gen_fn_call_rug(&self, name: &str, args: &[Spanned<Expr>]) -> String {
        match name {
            "abs" => {
                let a = self.gen_expr_rug(&args[0].node);
                format!("({}).abs()", a)
            }
            "min" if args.len() == 2 => {
                let a = self.gen_expr_rug(&args[0].node);
                let b = self.gen_expr_rug(&args[1].node);
                format!("{{ let _a = {}; let _b = {}; if _a < _b {{ _a }} else {{ _b }} }}", a, b)
            }
            "max" if args.len() == 2 => {
                let a = self.gen_expr_rug(&args[0].node);
                let b = self.gen_expr_rug(&args[1].node);
                format!("{{ let _a = {}; let _b = {}; if _a > _b {{ _a }} else {{ _b }} }}", a, b)
            }
            "to_int" | "to_float" => {
                // to_int on a float: convert
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                format!("Integer::from({} as i64)", a)
            }
            "to_string" => {
                self.gen_expr_rug_string(&Expr::FnCall { name: name.to_string(), args: args.to_vec() })
            }
            "floor" | "ceil" | "round" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                let method = name;
                format!("Integer::from(({}).{}() as i64)", a, method)
            }
            other => {
                // Call to another native handler
                if self.siblings.contains(other) {
                    let arg_strs: Vec<String> = args.iter()
                        .map(|a| {
                            let ty = self.infer_expr_type(&a.node);
                            self.gen_expr(&a.node, ty)
                        })
                        .collect();
                    format!("handler_{}({})", other, arg_strs.join(", "))
                } else {
                    format!("Integer::from(0i64) /* unknown fn: {} */", other)
                }
            }
        }
    }
}

/// Check if a handler body uses random() anywhere
fn body_uses_random(body: &[Spanned<Statement>]) -> bool {
    body.iter().any(|s| stmt_uses_random(&s.node))
}

fn stmt_uses_random(stmt: &Statement) -> bool {
    match stmt {
        Statement::Let { value, .. } | Statement::Assign { value, .. } | Statement::Return { value } => {
            expr_uses_random(&value.node)
        }
        Statement::If { condition, then_body, else_body } => {
            expr_uses_random(&condition.node) || body_uses_random(then_body) || body_uses_random(else_body)
        }
        Statement::While { condition, body } => {
            expr_uses_random(&condition.node) || body_uses_random(body)
        }
        Statement::For { iter, body, .. } => {
            expr_uses_random(&iter.node) || body_uses_random(body)
        }
        Statement::ExprStmt { expr } => expr_uses_random(&expr.node),
        _ => false,
    }
}

fn expr_uses_random(expr: &Expr) -> bool {
    match expr {
        Expr::FnCall { name, args } => {
            name == "random" || args.iter().any(|a| expr_uses_random(&a.node))
        }
        Expr::BinaryOp { left, right, .. } | Expr::CmpOp { left, right, .. } => {
            expr_uses_random(&left.node) || expr_uses_random(&right.node)
        }
        Expr::Not(inner) => expr_uses_random(&inner.node),
        _ => false,
    }
}


/// Transform generated BigInt-style arithmetic to V-type in-place ops.
/// Patterns:
///   x = ((&x) * (&y));     → x.mul_v(&y);
///   x = ((&x) + V::S(1));  → x.add(1);
///   x = ((&x) + (&y));     → x.add_v(&y);
///   x = ((&x) - (&y));     → x.sub_v(&y);
///   Also handles: let t = (&b) patterns for temp variables
fn transform_v_arithmetic(code: &str) -> String {
    let mut result = String::new();
    for line in code.lines() {
        let trimmed = line.trim();
        // Pattern: x = ((&x) OP (&y));
        if let Some(eq_pos) = trimmed.find(" = ") {
            let lhs = trimmed[..eq_pos].trim();
            let rhs = trimmed[eq_pos + 3..].trim().trim_end_matches(';');

            // x = ((&x) + V::S(N)); → x.add(N);
            if let Some(rest) = rhs.strip_prefix(&format!("((&{}) + V::S(", lhs)) {
                if let Some(n) = rest.strip_suffix("))") {
                    result.push_str(&format!("{}{};\n", &line[..line.len()-trimmed.len()],
                        format!("{}.add({})", lhs, n)));
                    continue;
                }
            }
            if let Some(rest) = rhs.strip_prefix(&format!("((&{}) - V::S(", lhs)) {
                if let Some(n) = rest.strip_suffix("))") {
                    result.push_str(&format!("{}{};\n", &line[..line.len()-trimmed.len()],
                        format!("{}.sub({})", lhs, n)));
                    continue;
                }
            }

            // x = ((&x) * (&y)); → x.mul_v(&y);
            if let Some(rest) = rhs.strip_prefix(&format!("((&{}) * (&", lhs)) {
                if let Some(y) = rest.strip_suffix("))") {
                    result.push_str(&format!("{}{};\n", &line[..line.len()-trimmed.len()],
                        format!("{}.mul_v(&{})", lhs, y)));
                    continue;
                }
            }
            // x = ((&x) + (&y)); → x.add_v(&y);
            if let Some(rest) = rhs.strip_prefix(&format!("((&{}) + (&", lhs)) {
                if let Some(y) = rest.strip_suffix("))") {
                    result.push_str(&format!("{}{};\n", &line[..line.len()-trimmed.len()],
                        format!("{}.add_v(&{})", lhs, y)));
                    continue;
                }
            }
            // x = ((&x) - (&y)); → x.sub_v(&y);
            if let Some(rest) = rhs.strip_prefix(&format!("((&{}) - (&", lhs)) {
                if let Some(y) = rest.strip_suffix("))") {
                    result.push_str(&format!("{}{};\n", &line[..line.len()-trimmed.len()],
                        format!("{}.sub_v(&{})", lhs, y)));
                    continue;
                }
            }

            // let x: V = (&y);  →  let x: V = y.clone_v();
            if trimmed.starts_with("let ") && rhs.starts_with("(&") && rhs.ends_with(")") {
                let var = &rhs[2..rhs.len()-1];
                result.push_str(&format!("{}{} = {}.clone_v();\n", &line[..line.len()-trimmed.len()], &trimmed[..eq_pos], var));
                continue;
            }

            // x = (&y); → x = y.clone_v();
            if rhs.starts_with("(&") && rhs.ends_with(")") {
                let var = &rhs[2..rhs.len()-1];
                result.push_str(&format!("{}{} = {}.clone_v();\n", &line[..line.len()-trimmed.len()], lhs, var));
                continue;
            }
        }

        // while ((&i) <= (&n)) → while i.le_v(n) (if n is param)
        // while ((&i) < (&n)) → while i.lt_v(n)
        if trimmed.starts_with("while ") {
            let cond = trimmed.strip_prefix("while ").unwrap().trim_end_matches(" {").trim();
            if let Some(rest) = cond.strip_prefix("((&") {
                if let Some(inner) = rest.strip_suffix("))") {
                    if inner.contains(") <= (&") {
                        let parts: Vec<&str> = inner.splitn(2, ") <= (&").collect();
                        if parts.len() == 2 {
                            let indent = &line[..line.len()-trimmed.len()];
                            result.push_str(&format!("{}while {}.le_v(&{}) {{\n", indent, parts[0], parts[1]));
                            continue;
                        }
                    }
                    if inner.contains(") < (&") {
                        let parts: Vec<&str> = inner.splitn(2, ") < (&").collect();
                        if parts.len() == 2 {
                            let indent = &line[..line.len()-trimmed.len()];
                            result.push_str(&format!("{}while {}.lt_v(&{}) {{\n", indent, parts[0], parts[1]));
                            continue;
                        }
                    }
                }
            }
        }

        // Return: convert V to i64 or write BigInt buffer
        if trimmed.contains("_ret_val.to_i64()") {
            let line = line.replace("_ret_val.to_i64()", "_ret_val.to_i64()");
            result.push_str(&line);
            result.push('\n');
            continue;
        }

        // General: x = ((&y) OP (&z)); where OP is +,-,*
        if let Some(eq_pos) = trimmed.find(" = ") {
            let lhs = trimmed[..eq_pos].trim();
            let rhs = trimmed[eq_pos + 3..].trim().trim_end_matches(';');
            if rhs.starts_with("((&") && rhs.ends_with("))") {
                let inner = &rhs[1..rhs.len()-1]; // strip outer parens
                let mut matched = false;
                for (op_str, method) in [(" + ", "add_v"), (" - ", "sub_v"), (" * ", "mul_v"), (" / ", "div_v"), (" % ", "mod_v")] {
                    if let Some(pos) = inner.find(op_str) {
                        let left = inner[..pos].trim().trim_start_matches("(&").trim_end_matches(')');
                        let right = inner[pos+op_str.len()..].trim().trim_start_matches("(&").trim_end_matches(')');
                        let indent = &line[..line.len()-trimmed.len()];
                        // If lhs is a `let` binding, emit without wrapping block
                        // so the variable stays in the enclosing scope
                        if lhs.contains("let ") {
                            result.push_str(&format!("{}let mut _t = {}.clone_v(); _t.{}(&{}); {} = _t;\n",
                                indent, left, method, right, lhs));
                        } else {
                            result.push_str(&format!("{}{{ let mut _t = {}.clone_v(); _t.{}(&{}); {} = _t; }}\n",
                                indent, left, method, right, lhs));
                        }
                        matched = true;
                        break;
                    }
                }
                if matched { continue; }
            }
        }

        result.push_str(line);
        result.push('\n');
    }
    result
}
