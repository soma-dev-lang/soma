//! Generates Rust source from [native] handler AST.
//!
//! The output is a single .rs file with `#[no_mangle] pub extern "C"` functions
//! that can be compiled into a cdylib and loaded via FFI.

use crate::ast::*;
use std::collections::{HashMap, HashSet};

/// Type inferred for a local variable
#[derive(Debug, Clone, Copy, PartialEq)]
pub enum NativeType {
    Int,
    Float,
    Bool,
}

impl NativeType {
    fn rust_str(&self) -> &'static str {
        match self {
            NativeType::Int => "i64",
            NativeType::Float => "f64",
            NativeType::Bool => "bool",
        }
    }
}

/// Information about a native handler to be compiled
pub struct NativeHandler {
    pub cell_name: String,
    pub signal_name: String,
    pub params: Vec<Param>,
    pub body: Vec<Spanned<Statement>>,
}

/// FFI signature info: param types + return type
#[derive(Debug, Clone)]
pub struct NativeSig {
    pub fn_name: String,
    pub param_types: Vec<NativeType>,
    pub return_type: NativeType,
}

/// Generate Rust source for a set of native handlers in one cell.
pub fn generate_native_source(handlers: &[NativeHandler]) -> (String, Vec<NativeSig>) {
    let mut out = String::new();
    let mut sigs = Vec::new();

    out.push_str("// Generated by Soma compiler — [native] handlers\n");
    out.push_str("// Do not edit — regenerate from .cell source\n\n");

    // Check if any handler uses random()
    let uses_random = handlers.iter().any(|h| body_uses_random(&h.body));
    if uses_random {
        out.push_str("static mut _SOMA_RNG_STATE: u64 = 0x12345678_9abcdef0;\n\n");
        out.push_str("#[inline(always)]\nunsafe fn _soma_random() -> f64 {\n");
        out.push_str("    _SOMA_RNG_STATE ^= _SOMA_RNG_STATE >> 12;\n");
        out.push_str("    _SOMA_RNG_STATE ^= _SOMA_RNG_STATE << 25;\n");
        out.push_str("    _SOMA_RNG_STATE ^= _SOMA_RNG_STATE >> 27;\n");
        out.push_str("    let r = _SOMA_RNG_STATE.wrapping_mul(0x2545F4914F6CDD1D);\n");
        out.push_str("    (r >> 11) as f64 / ((1u64 << 53) as f64)\n");
        out.push_str("}\n\n");
    }

    // Build set of sibling native handler names for inter-call resolution
    let siblings: HashSet<String> = handlers.iter().map(|h| h.signal_name.clone()).collect();

    for handler in handlers {
        let mut gen = FnGenerator::new(&handler.params, &siblings);

        // Infer types from params
        for p in &handler.params {
            let ty = type_expr_to_native(&p.ty.node);
            gen.var_types.insert(p.name.clone(), ty);
        }

        // Pre-scan body to infer local variable types
        gen.infer_body_types(&handler.body);

        let fn_name = format!("handler_{}", handler.signal_name);

        // Build param list
        let param_str: String = handler.params.iter()
            .map(|p| {
                let ty = type_expr_to_native(&p.ty.node);
                format!("{}: {}", p.name, ty.rust_str())
            })
            .collect::<Vec<_>>()
            .join(", ");

        // Determine return type from body (look for return statements)
        let ret_type = gen.infer_return_type(&handler.body);

        out.push_str(&format!("#[no_mangle]\npub extern \"C\" fn {}({}) -> {} {{\n",
            fn_name, param_str, ret_type.rust_str()));

        for stmt in &handler.body {
            let code = gen.gen_stmt(&stmt.node, 1);
            out.push_str(&code);
        }

        // If the body doesn't end with an explicit return, add a default
        let last_is_return = handler.body.last()
            .map(|s| matches!(s.node, Statement::Return { .. }))
            .unwrap_or(false);
        if !last_is_return {
            let default = match ret_type {
                NativeType::Int => "    0i64\n",
                NativeType::Float => "    0.0f64\n",
                NativeType::Bool => "    false\n",
            };
            out.push_str(default);
        }

        out.push_str("}\n\n");

        let param_types: Vec<NativeType> = handler.params.iter()
            .map(|p| type_expr_to_native(&p.ty.node))
            .collect();

        // For >3 params, generate an array-based wrapper
        if param_types.len() > 3 {
            out.push_str(&format!("#[no_mangle]\npub extern \"C\" fn {}_arr(args: *const f64, _count: i64) -> f64 {{\n", fn_name));
            out.push_str("    unsafe {\n");
            let call_args: Vec<String> = handler.params.iter().enumerate().map(|(i, p)| {
                let ty = type_expr_to_native(&p.ty.node);
                match ty {
                    NativeType::Int => format!("*args.add({}) as i64", i),
                    NativeType::Float => format!("*args.add({})", i),
                    NativeType::Bool => format!("*args.add({}) != 0.0", i),
                }
            }).collect();
            out.push_str(&format!("        let r = {}({});\n", fn_name, call_args.join(", ")));
            match ret_type {
                NativeType::Float => out.push_str("        r\n"),
                NativeType::Int => out.push_str("        r as f64\n"),
                NativeType::Bool => out.push_str("        if r { 1.0 } else { 0.0 }\n"),
            }
            out.push_str("    }\n}\n\n");

            // Use the array wrapper as the exported symbol
            sigs.push(NativeSig {
                fn_name: format!("{}_arr", fn_name),
                param_types,
                return_type: ret_type,
            });
        } else {
            sigs.push(NativeSig {
                fn_name,
                param_types,
                return_type: ret_type,
            });
        }
    }

    (out, sigs)
}

fn type_expr_to_native(ty: &TypeExpr) -> NativeType {
    match ty {
        TypeExpr::Simple(name) => match name.as_str() {
            "Int" => NativeType::Int,
            "Float" => NativeType::Float,
            "Bool" => NativeType::Bool,
            _ => NativeType::Float, // fallback
        },
        _ => NativeType::Float,
    }
}

struct FnGenerator {
    var_types: HashMap<String, NativeType>,
    siblings: HashSet<String>,
}

impl FnGenerator {
    fn new(params: &[Param], siblings: &HashSet<String>) -> Self {
        let mut var_types = HashMap::new();
        for p in params {
            var_types.insert(p.name.clone(), type_expr_to_native(&p.ty.node));
        }
        Self {
            var_types,
            siblings: siblings.clone(),
        }
    }

    fn infer_body_types(&mut self, body: &[Spanned<Statement>]) {
        for stmt in body {
            self.infer_stmt_types(&stmt.node);
        }
    }

    fn infer_stmt_types(&mut self, stmt: &Statement) {
        match stmt {
            Statement::Let { name, value } => {
                let ty = self.infer_expr_type(&value.node);
                self.var_types.insert(name.clone(), ty);
            }
            Statement::Assign { name, value } => {
                let ty = self.infer_expr_type(&value.node);
                // Assignment can promote int to float
                if let Some(existing) = self.var_types.get(name) {
                    if *existing == NativeType::Int && ty == NativeType::Float {
                        self.var_types.insert(name.clone(), NativeType::Float);
                    }
                } else {
                    self.var_types.insert(name.clone(), ty);
                }
            }
            Statement::If { then_body, else_body, .. } => {
                for s in then_body { self.infer_stmt_types(&s.node); }
                for s in else_body { self.infer_stmt_types(&s.node); }
            }
            Statement::While { body, .. } => {
                for s in body { self.infer_stmt_types(&s.node); }
            }
            Statement::For { var, iter, body, .. } => {
                // for var in range(...) → var is Int
                self.var_types.insert(var.clone(), NativeType::Int);
                let _ = self.infer_expr_type(&iter.node);
                for s in body { self.infer_stmt_types(&s.node); }
            }
            _ => {}
        }
    }

    fn infer_expr_type(&self, expr: &Expr) -> NativeType {
        match expr {
            Expr::Literal(Literal::Int(_)) => NativeType::Int,
            Expr::Literal(Literal::Float(_)) => NativeType::Float,
            Expr::Literal(Literal::Bool(_)) => NativeType::Bool,
            Expr::Ident(name) => {
                self.var_types.get(name).copied().unwrap_or(NativeType::Float)
            }
            Expr::BinaryOp { left, op, right } => {
                let lt = self.infer_expr_type(&left.node);
                let rt = self.infer_expr_type(&right.node);
                match op {
                    BinOp::Div => NativeType::Float, // division always float
                    BinOp::And | BinOp::Or => NativeType::Bool,
                    _ => {
                        if lt == NativeType::Float || rt == NativeType::Float {
                            NativeType::Float
                        } else {
                            lt // both Int → Int, both Bool → Bool
                        }
                    }
                }
            }
            Expr::CmpOp { .. } => NativeType::Bool,
            Expr::Not(_) => NativeType::Bool,
            Expr::FnCall { name, .. } => {
                match name.as_str() {
                    "random" | "sqrt" | "log" | "exp" | "pow" | "sin" | "cos" => NativeType::Float,
                    "len" | "floor" | "ceil" | "round" => NativeType::Int,
                    "abs" | "min" | "max" => {
                        // Depends on args, default to float
                        NativeType::Float
                    }
                    _ => NativeType::Float,
                }
            }
            _ => NativeType::Float,
        }
    }

    fn infer_return_type(&self, body: &[Spanned<Statement>]) -> NativeType {
        // Find the first return statement and infer from its expression
        for stmt in body {
            match &stmt.node {
                Statement::Return { value } => {
                    return self.infer_expr_type(&value.node);
                }
                Statement::If { then_body, else_body, .. } => {
                    if let Some(ty) = self.find_return_type(then_body) {
                        return ty;
                    }
                    if let Some(ty) = self.find_return_type(else_body) {
                        return ty;
                    }
                }
                _ => {}
            }
        }
        // No explicit return — check last expression
        if let Some(last) = body.last() {
            if let Statement::ExprStmt { expr } = &last.node {
                return self.infer_expr_type(&expr.node);
            }
        }
        NativeType::Float // default
    }

    fn find_return_type(&self, body: &[Spanned<Statement>]) -> Option<NativeType> {
        for stmt in body {
            if let Statement::Return { value } = &stmt.node {
                return Some(self.infer_expr_type(&value.node));
            }
        }
        None
    }

    fn indent(level: usize) -> String {
        "    ".repeat(level)
    }

    fn gen_stmt(&self, stmt: &Statement, indent: usize) -> String {
        let ind = Self::indent(indent);
        match stmt {
            Statement::Let { name, value } => {
                let ty = self.var_types.get(name).copied().unwrap_or(NativeType::Float);
                let expr = self.gen_expr(&value.node, ty);
                format!("{}let mut {}: {} = {};\n", ind, name, ty.rust_str(), expr)
            }
            Statement::Assign { name, value } => {
                let ty = self.var_types.get(name).copied().unwrap_or(NativeType::Float);
                let expr = self.gen_expr(&value.node, ty);
                format!("{}{} = {};\n", ind, name, expr)
            }
            Statement::Return { value } => {
                let ret_ty = self.infer_expr_type(&value.node);
                let expr = self.gen_expr(&value.node, ret_ty);
                format!("{}return {};\n", ind, expr)
            }
            Statement::If { condition, then_body, else_body } => {
                let cond = self.gen_expr(&condition.node, NativeType::Bool);
                let mut s = format!("{}if {} {{\n", ind, cond);
                for st in then_body {
                    s.push_str(&self.gen_stmt(&st.node, indent + 1));
                }
                if else_body.is_empty() {
                    s.push_str(&format!("{}}}\n", ind));
                } else {
                    s.push_str(&format!("{}}} else {{\n", ind));
                    for st in else_body {
                        s.push_str(&self.gen_stmt(&st.node, indent + 1));
                    }
                    s.push_str(&format!("{}}}\n", ind));
                }
                s
            }
            Statement::While { condition, body } => {
                let cond = self.gen_expr(&condition.node, NativeType::Bool);
                let mut s = format!("{}while {} {{\n", ind, cond);
                for st in body {
                    s.push_str(&self.gen_stmt(&st.node, indent + 1));
                }
                s.push_str(&format!("{}}}\n", ind));
                s
            }
            Statement::For { var, iter, body } => {
                let iter_code = self.gen_for_iter(&iter.node);
                let mut s = format!("{}for {} in {} {{\n", ind, var, iter_code);
                for st in body {
                    s.push_str(&self.gen_stmt(&st.node, indent + 1));
                }
                s.push_str(&format!("{}}}\n", ind));
                s
            }
            Statement::Break => format!("{}break;\n", ind),
            Statement::Continue => format!("{}continue;\n", ind),
            Statement::ExprStmt { expr } => {
                let code = self.gen_expr(&expr.node, NativeType::Float);
                format!("{}{};\n", ind, code)
            }
            _ => format!("{}// unsupported statement\n", ind),
        }
    }

    fn gen_for_iter(&self, expr: &Expr) -> String {
        // range(a, b) → a..b
        if let Expr::FnCall { name, args } = expr {
            if name == "range" && args.len() == 2 {
                let a = self.gen_expr(&args[0].node, NativeType::Int);
                let b = self.gen_expr(&args[1].node, NativeType::Int);
                return format!("{}..{}", a, b);
            }
        }
        // Fallback
        format!("0..0 /* unsupported iterator */")
    }

    fn gen_expr(&self, expr: &Expr, target_ty: NativeType) -> String {
        match expr {
            Expr::Literal(Literal::Int(n)) => {
                if target_ty == NativeType::Float {
                    format!("{}.0f64", n)
                } else {
                    format!("{}i64", n)
                }
            }
            Expr::Literal(Literal::Float(f)) => format!("{}f64", f),
            Expr::Literal(Literal::Bool(b)) => format!("{}", b),
            Expr::Ident(name) => {
                let var_ty = self.var_types.get(name).copied().unwrap_or(NativeType::Float);
                if target_ty == NativeType::Float && var_ty == NativeType::Int {
                    format!("{} as f64", name)
                } else if target_ty == NativeType::Int && var_ty == NativeType::Float {
                    format!("{} as i64", name)
                } else {
                    name.clone()
                }
            }
            Expr::BinaryOp { left, op, right } => {
                let result_ty = self.infer_expr_type(expr);
                let lt = self.infer_expr_type(&left.node);
                let rt = self.infer_expr_type(&right.node);

                match op {
                    BinOp::Div => {
                        // Always use float division
                        let l = self.gen_expr(&left.node, NativeType::Float);
                        let r = self.gen_expr(&right.node, NativeType::Float);
                        let inner = format!("({} / {})", l, r);
                        if target_ty == NativeType::Int {
                            format!("({} as i64)", inner)
                        } else {
                            inner
                        }
                    }
                    BinOp::Mod => {
                        // Modulo: keep types consistent
                        let common = if lt == NativeType::Float || rt == NativeType::Float {
                            NativeType::Float
                        } else {
                            NativeType::Int
                        };
                        let l = self.gen_expr(&left.node, common);
                        let r = self.gen_expr(&right.node, common);
                        format!("({} % {})", l, r)
                    }
                    BinOp::And => {
                        let l = self.gen_expr(&left.node, NativeType::Bool);
                        let r = self.gen_expr(&right.node, NativeType::Bool);
                        format!("({} && {})", l, r)
                    }
                    BinOp::Or => {
                        let l = self.gen_expr(&left.node, NativeType::Bool);
                        let r = self.gen_expr(&right.node, NativeType::Bool);
                        format!("({} || {})", l, r)
                    }
                    _ => {
                        let common = if lt == NativeType::Float || rt == NativeType::Float {
                            NativeType::Float
                        } else {
                            result_ty
                        };
                        let l = self.gen_expr(&left.node, common);
                        let r = self.gen_expr(&right.node, common);
                        let op_str = match op {
                            BinOp::Add => "+",
                            BinOp::Sub => "-",
                            BinOp::Mul => "*",
                            _ => unreachable!(),
                        };
                        let inner = format!("({} {} {})", l, op_str, r);
                        // Coerce to target if needed
                        if target_ty == NativeType::Float && common == NativeType::Int {
                            format!("({} as f64)", inner)
                        } else if target_ty == NativeType::Int && common == NativeType::Float {
                            format!("({} as i64)", inner)
                        } else {
                            inner
                        }
                    }
                }
            }
            Expr::CmpOp { left, op, right } => {
                // Compare in the wider type
                let lt = self.infer_expr_type(&left.node);
                let rt = self.infer_expr_type(&right.node);
                let common = if lt == NativeType::Float || rt == NativeType::Float {
                    NativeType::Float
                } else {
                    NativeType::Int
                };
                let l = self.gen_expr(&left.node, common);
                let r = self.gen_expr(&right.node, common);
                let op_str = match op {
                    CmpOp::Lt => "<",
                    CmpOp::Gt => ">",
                    CmpOp::Le => "<=",
                    CmpOp::Ge => ">=",
                    CmpOp::Eq => "==",
                    CmpOp::Ne => "!=",
                };
                format!("({} {} {})", l, op_str, r)
            }
            Expr::Not(inner) => {
                let e = self.gen_expr(&inner.node, NativeType::Bool);
                format!("(!{})", e)
            }
            Expr::FnCall { name, args } => {
                self.gen_fn_call(name, args, target_ty)
            }
            _ => "/* unsupported expr */ 0".to_string(),
        }
    }

    fn gen_fn_call(&self, name: &str, args: &[Spanned<Expr>], target_ty: NativeType) -> String {
        match name {
            "random" => {
                "unsafe { _soma_random() }".to_string()
            }
            "sqrt" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                format!("({}).sqrt()", a)
            }
            "log" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                format!("({}).ln()", a)
            }
            "exp" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                format!("({}).exp()", a)
            }
            "pow" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                let b = self.gen_expr(&args[1].node, NativeType::Float);
                format!("({}).powf({})", a, b)
            }
            "abs" => {
                let a_ty = self.infer_expr_type(&args[0].node);
                let a = self.gen_expr(&args[0].node, a_ty);
                format!("({}).abs()", a)
            }
            "min" => {
                if args.len() == 2 {
                    let a_ty = self.infer_expr_type(&args[0].node);
                    let b_ty = self.infer_expr_type(&args[1].node);
                    let common = if a_ty == NativeType::Float || b_ty == NativeType::Float {
                        NativeType::Float
                    } else {
                        NativeType::Int
                    };
                    let a = self.gen_expr(&args[0].node, common);
                    let b = self.gen_expr(&args[1].node, common);
                    if common == NativeType::Float {
                        format!("({}).min({})", a, b) // f64::min
                    } else {
                        format!("std::cmp::min({}, {})", a, b)
                    }
                } else {
                    "0".to_string()
                }
            }
            "max" => {
                if args.len() == 2 {
                    let a_ty = self.infer_expr_type(&args[0].node);
                    let b_ty = self.infer_expr_type(&args[1].node);
                    let common = if a_ty == NativeType::Float || b_ty == NativeType::Float {
                        NativeType::Float
                    } else {
                        NativeType::Int
                    };
                    let a = self.gen_expr(&args[0].node, common);
                    let b = self.gen_expr(&args[1].node, common);
                    if common == NativeType::Float {
                        format!("({}).max({})", a, b)
                    } else {
                        format!("std::cmp::max({}, {})", a, b)
                    }
                } else {
                    "0".to_string()
                }
            }
            "floor" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                format!("({}).floor() as i64", a)
            }
            "ceil" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                format!("({}).ceil() as i64", a)
            }
            "round" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                format!("({}).round() as i64", a)
            }
            "sin" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                format!("({}).sin()", a)
            }
            "cos" => {
                let a = self.gen_expr(&args[0].node, NativeType::Float);
                format!("({}).cos()", a)
            }
            "len" => {
                let a = self.gen_expr(&args[0].node, NativeType::Int);
                format!("/* len */ 0i64") // simplified — lists not yet supported in codegen
            }
            "nth" => {
                format!("/* nth */ 0") // simplified
            }
            "range" => {
                // Used in for loops, shouldn't appear as expression
                if args.len() == 2 {
                    let a = self.gen_expr(&args[0].node, NativeType::Int);
                    let b = self.gen_expr(&args[1].node, NativeType::Int);
                    format!("({}..{})", a, b)
                } else {
                    "(0..0)".to_string()
                }
            }
            other => {
                // Call to another native handler in the same cell
                let arg_strs: Vec<String> = args.iter()
                    .map(|a| {
                        let ty = self.infer_expr_type(&a.node);
                        self.gen_expr(&a.node, ty)
                    })
                    .collect();
                format!("handler_{}({})", other, arg_strs.join(", "))
            }
        }
    }
}

/// Check if a handler body uses random() anywhere
fn body_uses_random(body: &[Spanned<Statement>]) -> bool {
    body.iter().any(|s| stmt_uses_random(&s.node))
}

fn stmt_uses_random(stmt: &Statement) -> bool {
    match stmt {
        Statement::Let { value, .. } | Statement::Assign { value, .. } | Statement::Return { value } => {
            expr_uses_random(&value.node)
        }
        Statement::If { condition, then_body, else_body } => {
            expr_uses_random(&condition.node) || body_uses_random(then_body) || body_uses_random(else_body)
        }
        Statement::While { condition, body } => {
            expr_uses_random(&condition.node) || body_uses_random(body)
        }
        Statement::For { iter, body, .. } => {
            expr_uses_random(&iter.node) || body_uses_random(body)
        }
        Statement::ExprStmt { expr } => expr_uses_random(&expr.node),
        _ => false,
    }
}

fn expr_uses_random(expr: &Expr) -> bool {
    match expr {
        Expr::FnCall { name, args } => {
            name == "random" || args.iter().any(|a| expr_uses_random(&a.node))
        }
        Expr::BinaryOp { left, right, .. } | Expr::CmpOp { left, right, .. } => {
            expr_uses_random(&left.node) || expr_uses_random(&right.node)
        }
        Expr::Not(inner) => expr_uses_random(&inner.node),
        _ => false,
    }
}
